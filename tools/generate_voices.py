def _sanitize_fakeyou_cookie(raw: str) -> str:
    """Extract the session token value from various cookie formats.

    Users may pass the cookie in different ways:
      - Plain token value:       ``abc123``
      - With session= prefix:    ``session=abc123``
      - Full cookie header:      ``session=abc123; _ga=GA...``
      - With Cookie: prefix:     ``Cookie: session=abc123``

    This helper normalises all of these to the bare token value.
    """
    raw = raw.strip()
    if not raw:
        return ""

    # Strip optional "Cookie:" prefix (case-insensitive)
    if raw.lower().startswith("cookie:"):
        raw = raw[len("cookie:"):].strip()

    # If the string contains "session=", extract that cookie's value
    match = re.search(r"(?:^|;\s*)session=([^;]+)", raw)
    if match:
        return match.group(1).strip()

    # Otherwise treat the whole thing as a bare token value
    return raw


def _fakeyou_login(username: str, password: str) -> str:
    """Log in to FakeYou and return the session cookie token.

    Calls the ``/v1/login`` endpoint with the given credentials and
    extracts the ``session`` cookie from the response.

    Raises
    ------
    RuntimeError
        If login fails or no session cookie is returned.
    """
    resp = _requests.post(
        _FAKEYOU_LOGIN_URL,
        json={"username_or_email": username, "password": password},
        headers={"Content-Type": "application/json", "Accept": "application/json"},
        timeout=30,
    )

    if resp.status_code == 401:
        raise RuntimeError(
            "FakeYou login failed: invalid username/email or password."
        )
    resp.raise_for_status()

    result = resp.json()
    if not result.get("success"):
        raise RuntimeError(
            f"FakeYou login failed: "
            f"{result.get('error_reason', 'unknown error')}"
        )

    # Extract session cookie from the response Set-Cookie header.
    # resp.cookies is preferred; the raw header fallback is defensive
    # in case the requests library's cookie jar misses it.
    session_cookie = resp.cookies.get("session", "")
    if not session_cookie:
        # Try parsing from raw Set-Cookie header as fallback
        set_cookie = resp.headers.get("set-cookie", "")
        match = re.search(r"session=([^;]+)", set_cookie)
        if match:
            session_cookie = match.group(1)

    if not session_cookie:
        raise RuntimeError(
            "FakeYou login succeeded but no session cookie was returned."
        )

    log.info("FakeYou: logged in successfully as '%s'", username)
    return session_cookie


def _generate_fakeyou(text: str, out_wav: Path, cookie: str = "") -> None:
    """Synthesise *text* with the FakeYou Pokédex voice model.

    Submits a TTS inference job, polls until complete, and downloads
    the resulting WAV file to *out_wav*.

    Parameters
    ----------
    text : str
        The Pokédex entry text to speak.
    out_wav : Path
        Destination file path (WAV format).
    cookie : str
        Optional FakeYou session cookie for priority queue access.
        Without it, jobs go into the anonymous (slower) queue.
    """
    headers = {"Content-Type": "application/json", "Accept": "application/json"}
    if cookie:
        headers["Cookie"] = f"session={cookie}"

    # 1. Submit TTS inference job
    payload = {
        "tts_model_token": _FAKEYOU_MODEL_TOKEN,
        "uuid_idempotency_token": str(uuid.uuid4()),
        "inference_text": text,
    }
    resp = _requests.post(
        _FAKEYOU_INFERENCE_URL,
        headers=headers,
        json=payload,
        timeout=30,
    )
    resp.raise_for_status()
    result = resp.json()

    if not result.get("success"):
        raise RuntimeError(
            f"FakeYou inference request failed: "
            f"{result.get('error_reason', 'unknown error')}"
        )

    job_token = result.get("inference_job_token")
    if not job_token:
        raise RuntimeError("FakeYou returned no inference_job_token")

    log.debug("FakeYou job submitted: %s", job_token)

    # 2. Poll until the job finishes
    elapsed = 0
    status = "unknown"
    while elapsed < _FAKEYOU_MAX_WAIT:
        time.sleep(_FAKEYOU_POLL_INTERVAL)
        elapsed += _FAKEYOU_POLL_INTERVAL

        try:
            poll_resp = _requests.get(
                _FAKEYOU_JOB_URL.format(job_token),
                headers=headers,
                timeout=30,
            )
        except _requests.RequestException as exc:
            log.debug("FakeYou poll request failed: %s", exc)
            continue
        if poll_resp.status_code == 429:
            log.debug("FakeYou rate-limited, waiting %ds…", _FAKEYOU_RATE_LIMIT_WAIT)
            time.sleep(_FAKEYOU_RATE_LIMIT_WAIT)
            elapsed += _FAKEYOU_RATE_LIMIT_WAIT
            continue
        poll_resp.raise_for_status()
        state = poll_resp.json().get("state", {})
        status = state.get("status", "unknown")

        log.debug("  Job %s status: %s (%.0fs)", job_token, status, elapsed)

        if status == "complete_success":
            # Build candidate download URLs.
            # Prefer a full result URL returned by the API (future-proof);
            # fall back to constructing the URL from the audio path and
            # the known CDN base(s).
            audio_urls: list[str] = []

            full_url = state.get("maybe_result_url") or state.get("result_url")
            if full_url:
                audio_urls.append(full_url)

            wav_path = state.get("maybe_public_bucket_wav_audio_path")
            if wav_path:
                audio_urls.append(f"{_FAKEYOU_AUDIO_BASE}{wav_path}")
                audio_urls.append(f"{_FAKEYOU_AUDIO_BASE_FALLBACK}{wav_path}")

            if not audio_urls:
                raise RuntimeError("FakeYou job succeeded but returned no audio path")

            # Use plain headers for CDN download (no JSON content-type).
            # Include the session cookie when downloading from FakeYou's
            # own CDN, but omit it for third-party hosts.
            dl_headers: dict[str, str] = {}
            if cookie:
                dl_headers["Cookie"] = f"session={cookie}"

            # 3. Download the WAV file — try each candidate URL in order
            last_exc: Exception | None = None
            for audio_url in audio_urls:
                # Only send the session cookie to FakeYou's own domains;
                # skip it for third-party CDNs (e.g. googleapis.com).
                parsed_host = urllib.parse.urlparse(audio_url).hostname or ""
                is_fakeyou = (
                    parsed_host == "fakeyou.com"
                    or parsed_host.endswith(".fakeyou.com")
                )
                use_headers = dl_headers if is_fakeyou else {}
                for attempt in range(1, _FAKEYOU_DOWNLOAD_RETRIES + 1):
                    try:
                        audio_resp = _requests.get(
                            audio_url, headers=use_headers, timeout=60,
                        )
                        audio_resp.raise_for_status()
                        out_wav.write_bytes(audio_resp.content)
                        log.debug("Downloaded FakeYou audio: %s", out_wav)
                        return
                    except _requests.RequestException as exc:
                        last_exc = exc
                        if attempt < _FAKEYOU_DOWNLOAD_RETRIES:
                            log.debug(
                                "FakeYou download attempt %d/%d failed (%s), "
                                "retrying in %ds…",
                                attempt, _FAKEYOU_DOWNLOAD_RETRIES, exc,
                                _FAKEYOU_DOWNLOAD_BACKOFF,
                            )
                            time.sleep(_FAKEYOU_DOWNLOAD_BACKOFF)
                log.warning(
                    "All %d download retries exhausted for URL: %s",
                    _FAKEYOU_DOWNLOAD_RETRIES, audio_url,
                )

            raise RuntimeError(
                f"Failed to download FakeYou audio from "
                f"{audio_urls}: {last_exc}"
            ) from last_exc

        if status in ("dead", "attempt_failed", "complete_failure"):
            raise RuntimeError(f"FakeYou job failed with status: {status}")

        # "pending", "started", "attempt_pending" → keep polling

    raise RuntimeError(
        f"FakeYou job timed out after {_FAKEYOU_MAX_WAIT}s (last status: {status})"
    )


def _apply_dexter_effect(audio: "AudioSegment") -> "AudioSegment":
    """
    Apply post-processing to make the voice sound like Dexter from the
    classic Pokémon anime:

    1. High-pass filter  — removes bass, gives a thin, broadcast-radio quality
    2. Low-pass filter   — softens harsh high frequencies
    3. Echo overlay      — adds the characteristic resonant 'robot' quality
    4. Normalise         — ensures consistent loudness
    """
    # Band-pass simulation: cut below 350 Hz and above 4 000 Hz
    audio = audio.high_pass_filter(350)
    audio = audio.low_pass_filter(4000)

    # Subtle echo: quieter copy delayed by ~35 ms gives robotic resonance
    echo = audio - 13          # 13 dB quieter
    audio = audio.overlay(echo, position=35)

    audio = normalize(audio)
    return audio


def generate_voice_file(text: str, dest_ogg: Path, backend: str = "auto",
                        voice_index: int = 0,
                        fakeyou_cookie: str = "") -> tuple[bool, str]:
    """
    Generate a single voiced OGG file at *dest_ogg*.

    Returns (True, "") on success, (False, reason) on failure.
    """
    dest_ogg = Path(dest_ogg)
    dest_ogg.parent.mkdir(parents=True, exist_ok=True)

    with tempfile.TemporaryDirectory() as tmp_dir:
        tmp = Path(tmp_dir)

        # ---- step 1: raw TTS -----------------------------------------------
        raw_path: Path | None = None
        used_fakeyou = False
        failure_reason = ""

        if backend == "fakeyou" and HAS_REQUESTS:
            raw_path = tmp / "raw.wav"
            try:
                _generate_fakeyou(text, raw_path, cookie=fakeyou_cookie)
                used_fakeyou = True
            except Exception as exc:
                log.error("FakeYou failed: %s", exc)
                failure_reason = str(exc)
                raw_path = None

        if raw_path is None and backend in ("pyttsx3", "auto") and HAS_PYTTSX3:
            raw_path = tmp / "raw.wav"
            try:
                _generate_pyttsx3(text, raw_path, voice_index=voice_index)
                failure_reason = ""
            except Exception as exc:
                log.debug("pyttsx3 failed: %s", exc)
                if not failure_reason:
                    failure_reason = str(exc)
                raw_path = None

        if raw_path is None and backend in ("gtts", "auto") and HAS_GTTS:
            raw_path = tmp / "raw.mp3"
            try:
                _generate_gtts(text, raw_path)
                failure_reason = ""
            except Exception as exc:
                log.debug("gTTS failed: %s", exc)
                if not failure_reason:
                    failure_reason = str(exc)
                raw_path = None

        if raw_path is None or not raw_path.exists():
            reason = failure_reason or "No TTS backend produced output"
            log.error(
                "No TTS backend produced output.  "
                "Install pyttsx3 (offline) or gTTS (online), "
                "or use --backend fakeyou (requires internet)."
            )
            return False, reason

        # ---- step 2: audio effects + export --------------------------------
        # FakeYou already uses the real Pokédex AI voice model so the Dexter
        # post-processing effects are unnecessary and would degrade quality.
        if HAS_PYDUB and HAS_FFMPEG:
            try:
                audio = AudioSegment.from_file(str(raw_path))
                if not used_fakeyou:
                    audio = _apply_dexter_effect(audio)
                audio.export(
                    str(dest_ogg),
                    format="ogg",
                    parameters=["-q:a", "5"],
                )
            except Exception as exc:
                log.error("pydub processing failed: %s", exc)
                # Fall back to raw copy
                shutil.copy(str(raw_path), str(dest_ogg.with_suffix(raw_path.suffix)))
                return True, ""
        else:
            # No pydub or no ffmpeg — save the raw audio without Dexter effects.
            # The user has already been warned at startup; no per-file message needed.
            fallback = dest_ogg.with_suffix(raw_path.suffix)
            shutil.copy(str(raw_path), str(fallback))

    return True, ""


