def _ruby_symbol_str(sym) -> str:
    """Convert a rubymarshal Symbol to a plain uppercase string."""
    if hasattr(sym, "name"):
        return sym.name.upper()
    return str(sym).lstrip(":").upper()


def _ruby_bytes_str(val) -> str:
    """Decode a rubymarshal bytes/string value to a Python str."""
    if isinstance(val, bytes):
        return val.decode("utf-8", errors="replace")
    return str(val) if val else ""


def parse_species_dat(species_dat: Path) -> dict:
    """
    Parse a KIF/Essentials ``Data/species.dat`` file and return a
    ``{SPECIES_NAME: entry_text}`` dict.

    Requires the ``rubymarshal`` package (``pip install rubymarshal``).
    Returns an empty dict if the file cannot be read or rubymarshal is not
    installed.
    """
    entries: dict = {}

    if not HAS_RUBYMARSHAL:
        log.debug("rubymarshal not installed — cannot parse species.dat")
        return entries

    if not species_dat.is_file():
        log.debug("species.dat not found: %s", species_dat)
        return entries

    try:
        with open(species_dat, "rb") as fh:
            data = _rubymarshal_load(fh)
    except Exception as exc:
        log.warning("Failed to parse %s: %s", species_dat, exc)
        return entries

    for key, obj in data.items():
        # species.dat is keyed by both integer and Symbol; skip duplicates
        if isinstance(key, int):
            continue
        if not isinstance(obj, RubyObject):
            continue

        attrs = obj.attributes
        form = attrs.get("@form", 0)
        if form != 0:
            continue  # skip alternate forms

        species_id = attrs.get("@id")
        if species_id is None:
            continue
        species_name = _ruby_symbol_str(species_id)

        # "real_" prefix is Pokémon Essentials' convention for translatable
        # text fields (the non-translated source string).
        raw_entry = attrs.get("@real_pokedex_entry", "")
        entry_text = _clean_entry_text(_ruby_bytes_str(raw_entry))
        if entry_text:
            entries[species_name] = entry_text

    log.info("Parsed %d Pokédex entries from %s", len(entries), species_dat.name)
    return entries


def build_species_id_map(species_dat: Path) -> dict:
    """
    Parse ``Data/species.dat`` and return a ``{dex_number: SPECIES_NAME}``
    mapping.  Used to resolve numeric IDs in KIF's JSON Pokédex files.
    """
    id_map: dict = {}

    if not HAS_RUBYMARSHAL or not species_dat.is_file():
        return id_map

    try:
        with open(species_dat, "rb") as fh:
            data = _rubymarshal_load(fh)
    except Exception:
        return id_map

    for key, obj in data.items():
        if not isinstance(key, int):
            continue
        if not isinstance(obj, RubyObject):
            continue
        attrs = obj.attributes
        if attrs.get("@form", 0) != 0:
            continue
        species_id = attrs.get("@id")
        if species_id is None:
            continue
        id_map[key] = _ruby_symbol_str(species_id)

    return id_map


def parse_kif_fusion_json(game_dir: Path, id_map: dict) -> tuple[dict, dict]:
    """
    Parse KIF's ``Data/pokedex/dex.json`` and return two dicts:

    1. ``{(SPECIES1, SPECIES2): entry_text}`` — primary (first) entry per pair
    2. ``{(SPECIES1, SPECIES2): [entry_text, ...]}`` — variant entries (2nd, 3rd, …)

    *id_map* must be a ``{dex_number: SPECIES_NAME}`` mapping (from
    :func:`build_species_id_map`).
    """
    primary_entries: dict = {}
    variant_entries: dict = {}
    all_per_pair: dict = {}  # accumulates all entries per pair

    dex_json = game_dir / "Data" / "pokedex" / "dex.json"

    if not dex_json.is_file():
        return primary_entries, variant_entries

    try:
        content = dex_json.read_text(encoding="utf-8", errors="replace")
        records = json.loads(content)
    except (OSError, json.JSONDecodeError) as exc:
        log.warning("Cannot read %s: %s", dex_json, exc)
        return primary_entries, variant_entries

    for rec in records:
        sprite = rec.get("sprite", "")
        entry_text = _clean_entry_text(rec.get("entry", ""))
        if not entry_text or not sprite:
            continue

        # Sprite format: "head.body.png" or "head.body_variant.png"
        base = sprite.rsplit(".png", 1)[0] if sprite.endswith(".png") else sprite
        parts = base.split(".", 1)
        if len(parts) != 2:
            continue

        try:
            head_id = int(parts[0])
            # Body may contain variant suffix like "4a"; strip non-digits
            body_num = re.match(r"(\d+)", parts[1])
            if not body_num:
                continue
            body_id = int(body_num.group(1))
        except ValueError:
            continue

        head_name = id_map.get(head_id)
        body_name = id_map.get(body_id)
        if not head_name or not body_name:
            continue

        pair = (head_name, body_name)
        # Collect all unique entries per pair
        if pair not in all_per_pair:
            all_per_pair[pair] = []
        if entry_text not in all_per_pair[pair]:
            all_per_pair[pair].append(entry_text)

    # Split into primary (first entry) and variants (subsequent entries)
    for pair, texts in all_per_pair.items():
        primary_entries[pair] = texts[0]
        if len(texts) > 1:
            variant_entries[pair] = texts[1:]

    if primary_entries:
        log.info("Parsed %d fusion entries from %s", len(primary_entries), dex_json.name)
    if variant_entries:
        variant_count = sum(len(v) for v in variant_entries.values())
        log.info(
            "Found %d additional variant entries across %d fusion pairs",
            variant_count, len(variant_entries),
        )
    return primary_entries, variant_entries


# ---------------------------------------------------------------------------
# Registration.rb parsing (Data/Scripts/990_NPT/001_Registration.rb etc.)
# ---------------------------------------------------------------------------

# Regex to find each GameData::Species.register({...}) block.  The outer
# braces may contain nested hash literals (e.g. base_stats: { ... }), so we
# cannot just match the first '}' — instead we count brace depth.
_REGISTER_START_RE = re.compile(
    r"GameData::Species\.register\(\{", re.MULTILINE
)

# Field extractors applied to the text inside a single register block.
_REG_ID_RE = re.compile(r"(?:^|\s)id:\s+:(\w+)", re.MULTILINE)
_REG_ID_NUMBER_RE = re.compile(r"(?:^|\s)id_number:\s+(\d+)", re.MULTILINE)
_REG_DEX_ENTRY_RE = re.compile(
    r'pokedex_entry:\s*"((?:[^"\\]|\\.)*)"', re.DOTALL
)


def _extract_register_blocks(content: str) -> list[str]:
    """Return the text of each ``GameData::Species.register({...})`` block.

    Handles nested braces (e.g. ``base_stats: { ... }``) by tracking brace
    depth rather than relying on a single closing ``}``.
    """
    blocks: list[str] = []
    for m in _REGISTER_START_RE.finditer(content):
        # Start just after the opening '{'
        start = m.end()
        depth = 1
        i = start
        while i < len(content) and depth > 0:
            ch = content[i]
            if ch == "{":
                depth += 1
            elif ch == "}":
                depth -= 1
            elif ch == '"':
                # Skip over string literals so braces inside strings are
                # not counted.
                i += 1
                while i < len(content) and content[i] != '"':
                    if content[i] == "\\":
                        i += 1  # skip escaped character
                    i += 1
            elif ch == "#":
                # Skip single-line comments
                while i < len(content) and content[i] != "\n":
                    i += 1
            i += 1
        # depth == 0 → we found the matching '})'; extract the inner text
        if depth == 0:
            blocks.append(content[start : i - 1])
    return blocks


def parse_registration_rb(rb_file: Path) -> tuple[dict, dict]:
    """
    Parse a KIF Ruby registration script and return two dicts:

    1. ``{SPECIES_NAME: entry_text}``  — Pokédex descriptions
    2. ``{id_number: SPECIES_NAME}``   — numeric-ID-to-name mapping (for
       fusion resolution via :func:`parse_kif_fusion_json`)

    The expected format is ``GameData::Species.register({...})`` blocks
    containing at minimum ``id:``, ``id_number:``, and ``pokedex_entry:``
    fields (KIF's ``Data/Scripts/990_NPT/001_Registration.rb``).
    """
    entries: dict = {}
    id_map: dict = {}

    if not rb_file.is_file():
        log.debug("Registration script not found: %s", rb_file)
        return entries, id_map

    try:
        content = rb_file.read_text(encoding="utf-8", errors="replace")
    except OSError as exc:
        log.warning("Cannot read %s: %s", rb_file, exc)
        return entries, id_map

    blocks = _extract_register_blocks(content)

    for block in blocks:
        id_match = _REG_ID_RE.search(block)
        if not id_match:
            continue
        species_name = id_match.group(1).upper()

        # id_number (optional — only needed for fusion ID mapping)
        num_match = _REG_ID_NUMBER_RE.search(block)
        if num_match:
            try:
                id_number = int(num_match.group(1))
                id_map[id_number] = species_name
            except ValueError:
                pass

        # pokedex_entry
        entry_match = _REG_DEX_ENTRY_RE.search(block)
        if not entry_match:
            continue
        raw_text = entry_match.group(1)
        entry_text = _clean_entry_text(raw_text)
        if entry_text:
            entries[species_name] = entry_text

    if entries:
        log.info(
            "Parsed %d Pokédex entries (%d with id_number) from %s",
            len(entries),
            len(id_map),
            rb_file.name,
        )
    return entries, id_map


def find_registration_scripts(game_dir: Path) -> list[Path]:
    """Discover Registration.rb files under ``Data/Scripts/``.

    Returns a list of paths, checking:
    - ``Data/Scripts/990_NPT/001_Registration.rb`` (primary known location)
    - Any ``*.rb`` files under ``Data/Scripts/990_NPT/``
    - Any file matching ``**/001_Registration.rb`` elsewhere in ``Data/Scripts/``
    """
    scripts_dir = game_dir / "Data" / "Scripts"
    found: list[Path] = []
    seen: set[Path] = set()

    # Primary known location
    primary = scripts_dir / "990_NPT" / "001_Registration.rb"
    if primary.is_file():
        found.append(primary)
        seen.add(primary.resolve())

    # Any other .rb files in the 990_NPT directory
    npt_dir = scripts_dir / "990_NPT"
    if npt_dir.is_dir():
        for rb in sorted(npt_dir.glob("*.rb")):
            if rb.resolve() not in seen:
                found.append(rb)
                seen.add(rb.resolve())

    # Glob for Registration.rb elsewhere in Data/Scripts/
    if scripts_dir.is_dir():
        for rb in sorted(scripts_dir.glob("**/001_Registration.rb")):
            if rb.resolve() not in seen:
                found.append(rb)
                seen.add(rb.resolve())

    return found


