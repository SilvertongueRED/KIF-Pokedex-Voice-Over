def main(argv=None) -> int:
    parser = build_arg_parser()
    args = parser.parse_args(argv)

    if args.verbose:
        log.setLevel(logging.DEBUG)
        logging.getLogger().setLevel(logging.DEBUG)

    # ---- list-voices shortcut -----------------------------------------------
    if args.list_voices:
        return list_voices()

    # ---- validate required args ---------------------------------------------
    if not args.game_dir:
        parser.error("--game-dir is required (unless using --list-voices).")

    game_dir = Path(args.game_dir)
    if not game_dir.is_dir():
        log.error("Game directory not found: %s", game_dir)
        return 1

    # ---- check at least one TTS backend is available ------------------------
    fakeyou_cookie = args.fakeyou_cookie or os.environ.get("FAKEYOU_COOKIE", "")
    fakeyou_cookie = _sanitize_fakeyou_cookie(fakeyou_cookie)

    # FakeYou login: obtain a fresh session cookie from username/password
    fakeyou_user = args.fakeyou_username or os.environ.get("FAKEYOU_USERNAME", "")
    fakeyou_pass = args.fakeyou_password or os.environ.get("FAKEYOU_PASSWORD", "")
    if fakeyou_user and fakeyou_pass and not fakeyou_cookie:
        if not HAS_REQUESTS:
            log.error(
                "FakeYou login requires the 'requests' package.\n"
                "Run:  pip install requests"
            )
            return 1
        try:
            fakeyou_cookie = _fakeyou_login(fakeyou_user, fakeyou_pass)
        except RuntimeError as exc:
            log.error("FakeYou login failed: %s", exc)
            return 1

    if args.backend == "fakeyou":
        if not HAS_REQUESTS:
            log.error(
                "FakeYou backend requires the 'requests' package.\n"
                "Run:  pip install requests"
            )
            return 1
        if fakeyou_cookie:
            log.info("FakeYou: using authenticated session (cookie provided)")
        else:
            log.info(
                "FakeYou: no cookie provided — using anonymous queue (slower).\n"
                "  Set FAKEYOU_COOKIE env var or use --fakeyou-cookie for "
                "priority access."
            )
    elif not HAS_PYTTSX3 and not HAS_GTTS:
        log.error(
            "No TTS backend found.  "
            "Run:  pip install pyttsx3       (offline, generic voice)\n"
            "  or: pip install gTTS          (online, generic voice)\n"
            "  or: pip install requests      (for --backend fakeyou — "
            "RECOMMENDED, real anime voice)"
        )
        return 1

    if not HAS_PYDUB:
        log.warning(
            "pydub is not installed — Dexter audio effects will be skipped.\n"
            "Run:  pip install -r tools/requirements.txt\n"
            "Also ensure ffmpeg is installed and on your PATH."
        )
    elif not HAS_FFMPEG:
        log.warning(
            "ffmpeg not found on PATH — Dexter audio effects will be skipped.\n"
            "Install ffmpeg and add it to your PATH:\n"
            "  Windows: https://ffmpeg.org/download.html  (add the bin/ folder to PATH)\n"
            "           or:  winget install ffmpeg  /  choco install ffmpeg\n"
            "  macOS:   brew install ffmpeg\n"
            "  Linux:   sudo apt install ffmpeg\n"
            "After installing, open a new terminal so the updated PATH takes effect."
        )

    # ---- resolve output directory -------------------------------------------
    output_dir = game_dir / "Audio" / "SE" / "Pokedex"
    output_dir.mkdir(parents=True, exist_ok=True)
    log.info("Output directory: %s", output_dir)

    # ---- parse Pokédex data -------------------------------------------------
    # Strategy: try Data/species.dat (KIF) first, then PBS/pokemon.txt (PIF),
    # then an explicit --pbs-file if given.
    all_entries: dict = {}
    species_id_map: dict = {}
    species_dat = game_dir / "Data" / "species.dat"

    # 1. KIF Data/species.dat (requires rubymarshal)
    if species_dat.is_file():
        if HAS_RUBYMARSHAL:
            all_entries = parse_species_dat(species_dat)
            species_id_map = build_species_id_map(species_dat)
        else:
            log.info(
                "Found %s but rubymarshal is not installed.\n"
                "  Run:  pip install rubymarshal\n"
                "  to enable reading Pokédex data directly from the KIF game data.",
                species_dat,
            )

    # 2. PBS/pokemon.txt (vanilla PIF or manually extracted)
    if not all_entries:
        pbs_file = Path(args.pbs_file) if args.pbs_file else game_dir / "PBS" / "pokemon.txt"
        if pbs_file.is_file():
            all_entries = parse_pbs_pokemon(pbs_file)

    # 3. Registration.rb scripts (Data/Scripts/990_NPT/001_Registration.rb
    #    and similar) — supplementary source that adds species which may not
    #    be present in species.dat or PBS.
    reg_files: list[Path] = []
    if args.registration_file:
        reg_files = [Path(p) for p in args.registration_file]
    else:
        reg_files = find_registration_scripts(game_dir)

    reg_total_entries = 0
    for rf in reg_files:
        rb_entries, rb_id_map = parse_registration_rb(rf)
        # Merge without overwriting — species.dat / PBS entries take priority
        for name, text in rb_entries.items():
            if name not in all_entries:
                all_entries[name] = text
                reg_total_entries += 1
        for num, name in rb_id_map.items():
            if num not in species_id_map:
                species_id_map[num] = name
    if reg_total_entries:
        log.info(
            "Registration scripts added %d new Pokédex entries (total now %d)",
            reg_total_entries,
            len(all_entries),
        )

    # 4. Give up with a helpful error
    if not all_entries:
        has_species_dat = species_dat.is_file()
        if has_species_dat and not HAS_RUBYMARSHAL:
            log.error(
                "No Pokédex entries found.\n"
                "  Your game has Data/species.dat but rubymarshal is not installed.\n"
                "  Fix:  pip install rubymarshal"
            )
        else:
            log.error(
                "No Pokédex entries found.\n"
                "  Make sure --game-dir points to the KIF game root directory\n"
                "  (the folder that contains Game.exe and the Data/ directory).\n"
                "  Checked:\n"
                "    - %s  (not found)\n"
                "    - %s  (not found)\n"
                "    - Data/Scripts/**/001_Registration.rb  (not found)\n"
                "  If your PBS file is elsewhere, use --pbs-file to specify its path.\n"
                "  If you have a Registration.rb file, use --registration-file.",
                species_dat,
                game_dir / "PBS" / "pokemon.txt",
            )
        return 1

    # ---- filter to single species if requested ------------------------------
    if args.species:
        key = args.species.strip().upper()
        if key not in all_entries:
            log.error("Species '%s' not found in Pokédex data.", key)
            return 1
        all_entries = {key: all_entries[key]}

    # ---- load existing failure log ------------------------------------------
    failure_log_path = output_dir / "failed_entries.json"
    failure_log: dict = {}
    if failure_log_path.exists():
        try:
            with open(failure_log_path, encoding="utf-8") as fh:
                failure_log = json.load(fh)
        except Exception as exc:
            log.warning("Could not read failure log %s: %s", failure_log_path, exc)
            failure_log = {}

    # ---- build fusion entry map (needed even for --retry-failed) ------------
    fusion_entries: dict = {}
    fusion_variant_entries: dict = {}  # {(SP1, SP2): [text2, text3, …]}
    if args.fusions:
        if species_id_map:
            fusion_entries, fusion_variant_entries = parse_kif_fusion_json(game_dir, species_id_map)
        if not fusion_entries:
            fusion_entries = parse_fusion_entries(game_dir)
        if not fusion_entries and not args.retry_failed:
            log.info(
                "No fusion Pokédex entries found.  "
                "KIF may use auto-generated fusion descriptions that are not "
                "stored in data files."
            )

    # ---- build a lookup: filename → (text, dest) for retry mode ------------
    def _all_pending_entries() -> list[tuple[str, str, Path]]:
        """Return list of (filename, text, dest) for all entries to process.

        Includes primary fusion entries and any variant entries (named with
        a ``_v2``, ``_v3``, … suffix).  Variant files are generated alongside
        primary files but never overwrite them.
        """
        items = []
        for species_name, entry_text in sorted(all_entries.items()):
            dest = output_dir / f"dex_{species_name}.ogg"
            items.append((dest.name, entry_text, dest))
        if args.fusions:
            for (sp1, sp2), entry_text in sorted(fusion_entries.items()):
                dest = output_dir / f"dex_{sp1}_{sp2}.ogg"
                items.append((dest.name, entry_text, dest))
            # Variant entries for fusions with multiple dex descriptions
            for (sp1, sp2), variant_texts in sorted(fusion_variant_entries.items()):
                for idx, vtext in enumerate(variant_texts, start=2):
                    dest = output_dir / f"dex_{sp1}_{sp2}_v{idx}.ogg"
                    items.append((dest.name, vtext, dest))
        return items

    # ---- determine which entries to attempt ---------------------------------
    if args.retry_failed:
        if not failure_log:
            log.info("No failure log found at %s — nothing to retry.", failure_log_path)
            print("\nDone — generated: 0, skipped: 0, failed: 0")
            print(f"Audio files saved to: {output_dir}")
            return 0

        # Build lookup of all entries so we can find text for failed filenames
        all_pending = {fname: (text, dest) for fname, text, dest in _all_pending_entries()}
        retry_filenames = list(failure_log.keys())
        log.info(
            "Retry mode: attempting %d previously failed entries from %s",
            len(retry_filenames),
            failure_log_path,
        )
        pending: list[tuple[str, str, Path]] = []
        for fname in retry_filenames:
            if fname in all_pending:
                text, dest = all_pending[fname]
                pending.append((fname, text, dest))
            else:
                log.warning(
                    "Failed entry '%s' not found in current Pokédex data — skipping.",
                    fname,
                )
    else:
        pending = _all_pending_entries()

    # ---- load existing duration manifest (for merge on retry) ---------------
    durations_path = output_dir / "dex_durations.json"
    durations: dict = {}
    if durations_path.exists():
        try:
            with open(durations_path, encoding="utf-8") as fh:
                durations = json.load(fh)
        except Exception as exc:
            log.warning("Could not read duration manifest %s: %s", durations_path, exc)
            durations = {}

    # ---- process entries ----------------------------------------------------
    generated = skipped = failed = 0

    for filename, entry_text, dest in pending:
        # A fusion filename has two underscores: dex_SP1_SP2.ogg (count >= 2)
        # A regular filename has one:            dex_SPECIES.ogg  (count == 1)
        is_fusion = filename.count("_") >= 2

        if not args.overwrite and dest.exists() and not args.retry_failed:
            log.debug("Skipping %s (already exists)", filename)
            skipped += 1
            continue

        log.info("Generating%s: %s", " fusion" if is_fusion else "", filename)
        log.debug("  Text: %s", entry_text[:80])

        ok, reason = generate_voice_file(
            entry_text,
            dest,
            backend=args.backend,
            voice_index=args.voice,
            fakeyou_cookie=fakeyou_cookie,
        )
        if ok:
            generated += 1
            # Remove from failure log on success
            failure_log.pop(filename, None)
            # Record duration in manifest (requires pydub + ffmpeg)
            if HAS_PYDUB and HAS_FFMPEG and dest.exists():
                try:
                    audio = AudioSegment.from_file(str(dest))
                    durations[filename] = len(audio) / 1000.0
                except Exception as exc:
                    log.debug("Could not read duration for %s: %s", filename, exc)
        else:
            failed += 1
            failure_log[filename] = reason

    # ---- persist duration manifest ------------------------------------------
    if durations:
        try:
            with open(durations_path, "w", encoding="utf-8") as fh:
                json.dump(durations, fh, indent=2, sort_keys=True)
            log.info(
                "Duration manifest updated (%d entries): %s",
                len(durations),
                durations_path,
            )
        except Exception as exc:
            log.warning("Could not write duration manifest %s: %s", durations_path, exc)

    # ---- persist entry map (text → audio filename mapping) -----------------
    # Maps audio file stems (e.g. "dex_RATTATA_HOOTHOOT_v2") to the Pokédex
    # entry text that was used to generate the audio.  The mod uses this to
    # match the currently displayed entry text to the correct audio variant.
    entry_map: dict = {}
    for filename, entry_text, dest in _all_pending_entries():
        stem = dest.stem  # e.g. "dex_RATTATA_HOOTHOOT" or "dex_RATTATA_HOOTHOOT_v2"
        entry_map[stem] = entry_text

    if entry_map:
        entry_map_path = output_dir / "dex_entry_map.json"
        try:
            with open(entry_map_path, "w", encoding="utf-8") as fh:
                json.dump(entry_map, fh, indent=2, sort_keys=True)
            log.info(
                "Entry map written (%d entries): %s",
                len(entry_map),
                entry_map_path,
            )
        except Exception as exc:
            log.warning("Could not write entry map %s: %s", entry_map_path, exc)

    # ---- persist failure log ------------------------------------------------
    try:
        if failure_log:
            with open(failure_log_path, "w", encoding="utf-8") as fh:
                json.dump(failure_log, fh, indent=2)
            log.info(
                "Failure log updated (%d entries): %s",
                len(failure_log),
                failure_log_path,
            )
        elif failure_log_path.exists():
            # All entries succeeded — remove the now-empty log file
            failure_log_path.unlink()
    except Exception as exc:
        log.warning("Could not write failure log %s: %s", failure_log_path, exc)

    # ---- summary ------------------------------------------------------------
    print(
        f"\nDone — generated: {generated}, skipped: {skipped}, failed: {failed}"
    )
    print(f"Audio files saved to: {output_dir}")
    return 0 if failed == 0 else 1


if __name__ == "__main__":
    sys.exit(main())
