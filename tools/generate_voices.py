def list_voices() -> int:
    if not HAS_PYTTSX3:
        print("pyttsx3 is not installed.  Run:  pip install pyttsx3")
        return 1
    engine = pyttsx3.init()
    voices = engine.getProperty("voices")
    if not voices:
        print("No voices found.")
        engine.stop()
        return 0
    print("Available pyttsx3 voices:")
    for i, v in enumerate(voices):
        print(f"  [{i}]  {v.name}")
        print(f"        id: {v.id}")
    engine.stop()
    return 0


