# ---------------------------------------------------------------------------
# Auto-start the Fish-Speech server at game launch
# ---------------------------------------------------------------------------
# If the user has both fish_speech_enabled and fish_speech_autostart set,
# kick off the server now — in a background thread so the title screen is
# never delayed.  This gives the ~15-40 s warmup time to complete before the
# player ever opens a Pokédex entry, so the first read-out is fast.
#
# Works on first-ever launch (no manual server start required) because we
# fire here unconditionally as long as the settings allow it.
# ---------------------------------------------------------------------------
if PokedexVoiceOver.fish_speech_enabled?
  Thread.new do
    begin
      # Small delay so the title screen is fully up before we spawn the window
      sleep(1)
      PokedexVoiceOver._fish_speech_try_autostart
      PokedexVoiceOver.log("Game-launch auto-start: server spawn attempted")
    rescue => e
      PokedexVoiceOver.log("Game-launch auto-start error: #{e.class}: #{e.message}")
    end
  end
end

# ---------------------------------------------------------------------------
# Game-close: shut down the Fish-Speech sidecar so its terminal window goes
# away with the game.  Three layers of defence:
#
#   1. at_exit — fires on a normal exit (player closes the window from the
#      title screen, ALT-F4, etc.)  Sends POST /shutdown to the server.
#   2. trap("INT"/"TERM") — handles Ctrl+C or SIGTERM-style shutdowns.
#   3. PID-file termination — a fallback path that taskkill /T's the cmd.exe
#      wrapper window when HTTP shutdown alone isn't enough.
#
# An idle-shutdown watchdog inside server.py covers the case where the game
# crashes and none of the above run.
# ---------------------------------------------------------------------------
begin
  at_exit do
    begin
      PokedexVoiceOver._fish_speech_shutdown
    rescue => e
      begin
        PokedexVoiceOver.log("at_exit shutdown error: #{e.class}: #{e.message}")
      rescue
        # logging itself failed — nothing else we can do
      end
    end
  end
rescue => _e
  # at_exit isn't available in some embedded Ruby builds — silently ignore.
end

# Convert Ctrl+C / SIGTERM into a clean server shutdown too.  MKXP-Z on Windows
# does not deliver real POSIX signals so this is mostly a no-op there, but it
# costs nothing to wire up and helps on Linux/macOS.
%w[INT TERM].each do |sig|
  begin
    Signal.trap(sig) do
      begin
        PokedexVoiceOver._fish_speech_shutdown
      rescue
        # nothing useful to do from inside a signal handler
      end
      # Re-raise so the normal exit path still runs.
      exit
    end
  rescue ArgumentError
    # Signal not supported on this platform (e.g. SIGTERM on Windows) — skip.
  rescue => _e
    # Some embedded Rubies disallow re-trapping; ignore.
  end
end

# =============================================================================
# Mod Settings submenu scene (Stonewall's Mod Settings mod)
# =============================================================================
# Creates a dedicated Pokédex Voice Over settings submenu that opens when the
# user clicks the "Pokédex Voice Over" entry under the Interface category.
#
# Follows the same submenu pattern used by the Economy Mod and other KIF mods.
# See: https://github.com/Stonewallx/KIF-Mods/blob/main/Documentation/Mod%20Settings.md

class PokedexVoiceOverSettingsScene < PokemonOption_Scene
  include ModSettingsSpacing if defined?(ModSettingsSpacing)

  # Skip fade-in to avoid double-fade (outer pbFadeOutIn handles the transition)
  def pbFadeInAndShow(sprites, visiblesprites = nil)
    if visiblesprites
      visiblesprites.each { |s| sprites[s].visible = true }
    else
      sprites.each { |key, sprite| sprite.visible = true if sprite }
    end
  end

  def pbGetOptions(inloadscreen = false)
    options = []

    options << EnumOption.new(
      _INTL("Enable Voice Over"),
      [_INTL("Off"), _INTL("On")],
      proc { ModSettingsMenu.get(:pokedex_vo_enabled) || 0 },
      proc { |value| ModSettingsMenu.set(:pokedex_vo_enabled, value) },
      _INTL("Enable or disable Pokédex voice-over narration")
    )

    options << StoneSliderOption.new(
      _INTL("Voice Volume"),
      0, 100, 5,
      proc { ModSettingsMenu.get(:pokedex_vo_volume) || 80 },
      proc { |value| ModSettingsMenu.set(:pokedex_vo_volume, value) },
      _INTL("Adjust the volume of the Pokédex voice-over narration")
    )

    options << EnumOption.new(
      _INTL("Mute Music in Pokédex"),
      [_INTL("Off"), _INTL("On")],
      proc { ModSettingsMenu.get(:pokedex_vo_mute_music) || 0 },
      proc { |value| ModSettingsMenu.set(:pokedex_vo_mute_music, value) },
      _INTL("Mute background music while the Pokédex is open")
    )

    options << EnumOption.new(
      _INTL("Mute Music During Voice"),
      [_INTL("Off"), _INTL("On")],
      proc { ModSettingsMenu.get(:pokedex_vo_mute_during_voiceover) || 1 },
      proc { |value| ModSettingsMenu.set(:pokedex_vo_mute_during_voiceover, value) },
      _INTL("Mute music while the Pokédex entry is being read (e.g. after capture)")
    )

    options << EnumOption.new(
      _INTL("Re-read on Page Return"),
      [_INTL("Off"), _INTL("On")],
      proc { ModSettingsMenu.get(:pokedex_vo_replay_on_page_change) || 0 },
      proc { |value| ModSettingsMenu.set(:pokedex_vo_replay_on_page_change, value) },
      _INTL("Replay the voice-over when navigating back to the description page")
    )

    options << EnumOption.new(
      _INTL("Fish-Speech Voice (Primary)"),
      [_INTL("Off"), _INTL("On")],
      proc { v = ModSettingsMenu.get(:pokedex_vo_fish_speech_enabled); v.nil? ? 1 : v },
      proc { |value| ModSettingsMenu.set(:pokedex_vo_fish_speech_enabled, value) },
      _INTL("Use the offline Fish-Speech sidecar as the primary voice for every entry (requires fish_speech/setup.py to have been run)")
    )

    options << EnumOption.new(
      _INTL("Auto-Start Fish-Speech Server"),
      [_INTL("Off"), _INTL("On")],
      proc { v = ModSettingsMenu.get(:pokedex_vo_fish_speech_autostart); v.nil? ? 1 : v },
      proc { |value| ModSettingsMenu.set(:pokedex_vo_fish_speech_autostart, value) },
      _INTL("Auto-launch the Fish-Speech server when the game starts so it is ready before you open the Pokedex")
    )

    options << EnumOption.new(
      _INTL("TTS Fallback (Piper)"),
      [_INTL("Off"), _INTL("On")],
      proc { v = ModSettingsMenu.get(:pokedex_vo_tts_enabled); v.nil? ? 1 : v },
      proc { |value| ModSettingsMenu.set(:pokedex_vo_tts_enabled, value) },
      _INTL("Use Piper TTS to read auto-generated entries aloud when no pre-recorded audio exists (requires Piper to be installed)")
    )

    options = auto_insert_spacers(options) if defined?(ModSettingsSpacing) && respond_to?(:auto_insert_spacers)
    return options
  end

  def pbStartScene(inloadscreen = false)
    super

    # Set custom title
    @sprites["title"] = Window_UnformattedTextPokemon.newWithSize(
      _INTL("Pokédex Voice Over"), 0, 0, Graphics.width, 64, @viewport)

    # Apply current color theme and modsettings_menu flag
    if @sprites["option"]
      @sprites["option"].modsettings_menu = true if @sprites["option"].respond_to?(:modsettings_menu=)

      if defined?(ModSettingsMenu) && defined?(COLOR_THEMES)
        theme_index = ModSettingsMenu.get(:modsettings_color_theme) || 0
        theme_key = COLOR_THEMES.keys[theme_index]
        if theme_key
          theme = COLOR_THEMES[theme_key]
          if theme[:base] && theme[:shadow]
            @sprites["option"].nameBaseColor = theme[:base]
            @sprites["option"].nameShadowColor = theme[:shadow]
            @sprites["option"].selBaseColor = theme[:base]
            @sprites["option"].selShadowColor = theme[:shadow]
          end
        end
      end
    end

    # Initialize values
    for i in 0...@PokemonOptions.length
      @sprites["option"][i] = (@PokemonOptions[i].get || 0)
    end
    @sprites["option"].refresh

    pbFadeInAndShow(@sprites) { pbUpdate }
  end
end

# =============================================================================
# Mod Settings menu registration
# =============================================================================
# Registers a single "Pokédex Voice Over" button under the Interface category
# that opens the dedicated submenu scene above.
#
# When the Mod Settings system has already loaded, we register immediately.
# Otherwise we queue the registrations via $MOD_SETTINGS_PENDING_REGISTRATIONS
# so they are picked up once the Mod Settings system initialises later.

pokedex_vo_register_settings = proc {
  # Ensure default values are initialised in storage
  ModSettingsMenu.set(:pokedex_vo_enabled, 1) if ModSettingsMenu.get(:pokedex_vo_enabled).nil?
  ModSettingsMenu.set(:pokedex_vo_volume, 80) if ModSettingsMenu.get(:pokedex_vo_volume).nil?
  ModSettingsMenu.set(:pokedex_vo_mute_music, 1) if ModSettingsMenu.get(:pokedex_vo_mute_music).nil?
  ModSettingsMenu.set(:pokedex_vo_mute_during_voiceover, 1) if ModSettingsMenu.get(:pokedex_vo_mute_during_voiceover).nil?
  ModSettingsMenu.set(:pokedex_vo_replay_on_page_change, 0) if ModSettingsMenu.get(:pokedex_vo_replay_on_page_change).nil?
  ModSettingsMenu.set(:pokedex_vo_fish_speech_enabled, 1) if ModSettingsMenu.get(:pokedex_vo_fish_speech_enabled).nil?
  ModSettingsMenu.set(:pokedex_vo_fish_speech_autostart, 1) if ModSettingsMenu.get(:pokedex_vo_fish_speech_autostart).nil?
  ModSettingsMenu.set(:pokedex_vo_tts_enabled, 1) if ModSettingsMenu.get(:pokedex_vo_tts_enabled).nil?

  ModSettingsMenu.register(:pokedex_vo_submenu, {
    name: "Pokédex Voice Over",
    type: :button,
    description: "Configure Pokédex voice-over settings",
    on_press: proc {
      pbFadeOutIn {
        scene = PokedexVoiceOverSettingsScene.new
        screen = PokemonOptionScreen.new(scene)
        screen.pbStartScreen
      }
    },
    category: "Interface",
    searchable: [
      "voice over", "pokedex", "volume", "mute music", "narration",
      "dexter", "voice", "audio", "re-read", "page change",
      "capture", "mute during voiceover", "tts", "piper", "text to speech",
      "tts fallback", "auto-generated", "fish-speech", "fish speech",
      "voice clone", "auto-start"
    ]
  })
}

if defined?(ModSettingsMenu)
  # Mod Settings system is already available — register immediately.
  pokedex_vo_register_settings.call
else
  # Mod Settings system hasn't loaded yet — queue for later processing.
  $MOD_SETTINGS_PENDING_REGISTRATIONS ||= []
  $MOD_SETTINGS_PENDING_REGISTRATIONS << pokedex_vo_register_settings
end
