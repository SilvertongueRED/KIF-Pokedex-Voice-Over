PokedexVoiceOver.log("  defined?(PokemonPokedexInfo_Scene) = #{defined?(PokemonPokedexInfo_Scene).inspect}")

if defined?(PokemonPokedexInfo_Scene)
  PokedexVoiceOver.log("  PokemonPokedexInfo_Scene exists — installing hooks")
  PokedexVoiceOver.log("  Instance methods: #{PokemonPokedexInfo_Scene.instance_methods(false).sort.inspect}")

  class PokemonPokedexInfo_Scene
    POKEDEX_VO_ENTRY_PAGE = 1   # page index that shows the Pokédex description (KIF uses 1-based pages)

    # Helper: read the current species + fusion partner from scene state.
    #
    # Searches multiple sources in order of reliability after navigation:
    #   1. @dummyPokemon (explicitly refreshed by pbUpdateDummyPokemon)
    #   2. @dexlist[@index] (index is updated first during scrolling)
    #   3. @species and other direct ivars (may be stale after pbGoToNext)
    #
    # Uses instance_variable_defined? instead of rescue — Ruby ivars return
    # nil silently, so rescue never triggers.
    def pokedex_vo_current_species
      head = nil
      fused = nil

      # --- Strategy 0: @dummyPokemon (most reliable after navigation) ---
      # KIF creates/updates @dummyPokemon via pbUpdateDummyPokemon for each
      # dex entry.  It is the freshest source after pbGoToNext/pbGoToPrevious.
      if instance_variable_defined?(:@dummyPokemon) && @dummyPokemon
        dummy = @dummyPokemon
        PokedexVoiceOver.log("  @dummyPokemon class=#{dummy.class}, methods=#{([:species, :fused, :fused_species, :fusion_partner, :fused_with, :getSpecies, :id_number, :speciesName] & dummy.methods).inspect}")

        # Extract head species
        if dummy.respond_to?(:species)
          val = dummy.species rescue nil
          if val && !(val.is_a?(Integer) && val <= 0)
            head = val
            PokedexVoiceOver.log("  Head species from @dummyPokemon.species: #{val.inspect}")
          end
        end

        # Extract fusion partner from @dummyPokemon
        if fused.nil?
          [:fused, :fused_species, :fusion_partner, :fused_with, :fusion_body].each do |attr|
            next unless dummy.respond_to?(attr)
            begin
              val = dummy.send(attr)
              next if val.nil?
              next if val.is_a?(Integer) && val <= 0
              if val.is_a?(Array) && val.length > 0
                fused = val[0]
                PokedexVoiceOver.log("  Fused species from @dummyPokemon.#{attr}[0]: #{fused.inspect}")
              else
                fused = val
                PokedexVoiceOver.log("  Fused species from @dummyPokemon.#{attr}: #{fused.inspect}")
              end
              break
            rescue StandardError => e
              PokedexVoiceOver.log("  @dummyPokemon.#{attr} failed: #{e.message}")
            end
          end
        end
      end

      # --- Strategy 1: @dexlist[@index] (index is always up-to-date) ---
      if head.nil? && instance_variable_defined?(:@dexlist) && instance_variable_defined?(:@index)
        begin
          entry = instance_variable_get(:@dexlist)[instance_variable_get(:@index)]
          if entry
            if entry.is_a?(Array)
              head = entry[0]
            elsif entry.respond_to?(:species)
              head = entry.species
            elsif entry.is_a?(Hash)
              head = entry[:species] || entry[:id]
            else
              head = entry
            end
            PokedexVoiceOver.log("  Head species found via @dexlist[@index]: #{head.inspect}")
          end
        rescue StandardError => e
          PokedexVoiceOver.log("  @dexlist[@index] lookup failed: #{e.message}")
        end
      end

      # --- Strategy 2: Direct ivar names (may be stale after navigation) ---
      if head.nil?
        [:@species, :@current_species, :@dexdata].each do |var|
          if instance_variable_defined?(var)
            val = instance_variable_get(var)
            next if val.nil?
            head = val
            PokedexVoiceOver.log("  Head species found in #{var}: #{val.inspect}")
            break
          end
        end
      end

      if head.nil?
        PokedexVoiceOver.log("  WARNING: Could not find head species")
        vars = instance_variables.select { |v| v.to_s =~ /species|dexlist|dexdata|fused|fusion|pokemon/i }
        vars.each do |v|
          PokedexVoiceOver.log("    #{v} = #{instance_variable_get(v).inspect rescue '<error>'}")
        end
        PokedexVoiceOver.log("  All scene ivars: #{instance_variables.sort.inspect}")
      end

      # --- Fusion decomposition from composite integer ID ---
      # KIF encodes fused Pokémon as: head_id * NB_POKEMON + body_id.
      # We try to obtain a numeric composite ID from multiple sources:
      #   1. head itself (if it's already an integer > NB_POKEMON)
      #   2. @dummyPokemon.id_number (when head is a Symbol but id_number
      #      holds the composite integer — common in newer KIF builds)
      if fused.nil?
        composite = nil
        if head.is_a?(Integer) && head > 0
          composite = head
        elsif instance_variable_defined?(:@dummyPokemon) && @dummyPokemon
          # head was a Symbol — try id_number for the composite integer
          dummy = @dummyPokemon
          if dummy.respond_to?(:id_number)
            begin
              val = dummy.id_number
              composite = val if val.is_a?(Integer) && val > 0
              PokedexVoiceOver.log("  Fusion decomposition: id_number=#{val.inspect} (head was #{head.class})")
            rescue StandardError => e
              PokedexVoiceOver.log("  @dummyPokemon.id_number failed: #{e.message}")
            end
          end
        end

        if composite
          nb_pokemon = PokedexVoiceOver.nb_pokemon

          if nb_pokemon && nb_pokemon > 0 && composite > nb_pokemon
            body_id = composite % nb_pokemon
            head_id = composite / nb_pokemon
            if head_id > 0 && body_id > 0
              PokedexVoiceOver.log("  Detected fused species ID #{composite} => head=#{head_id}, body=#{body_id} (NB_POKEMON=#{nb_pokemon})")
              fused = body_id
              head = head_id
            end
          end
        end
      end

      # --- Separate fused-species ivars (fallback) ---
      if fused.nil?
        [:@fusedSpecies, :@dexSpecies2, :@speciesFused, :@fused_species, :@species2].each do |var|
          if instance_variable_defined?(var)
            val = instance_variable_get(var)
            next if val.nil?
            next if val.is_a?(Integer) && val <= 0
            fused = val
            PokedexVoiceOver.log("  Fused species found in #{var}: #{val.inspect}")
            break
          end
        end
      end

      # Cross-check: log if @species differs from @dummyPokemon (stale-state detection)
      if head && instance_variable_defined?(:@species)
        ivar_sp = instance_variable_get(:@species)
        if ivar_sp && ivar_sp != head
          PokedexVoiceOver.log("  NOTE: @species=#{ivar_sp.inspect} differs from detected head=#{head.inspect} (stale ivar?)")
        end
      end

      PokedexVoiceOver.log("  pokedex_vo_current_species => head=#{head.inspect}, fused=#{fused.inspect}")
      [head, fused]
    end

    # Helper: try to read the currently displayed Pokédex entry text from
    # the scene state.  Used to match the correct audio variant instead of
    # picking one at random.
    #
    # Tries multiple strategies since KIF/PIF versions may store the text
    # in different places.  Returns nil if the text cannot be determined
    # (the caller should fall back to random variant selection).
    #
    # Strategy order (most reliable first):
    #   1. @dummyPokemon attributes (refreshed by pbUpdateDummyPokemon)
    #   2. KIF-specific scene methods (getCustomDexEntry, etc.)
    #   3. Instance variable scan (may be stale after navigation)
    #   4. GameData lookups
    #   5. pbGetMessage fallback
    def pokedex_vo_displayed_text
      text = nil

      # Strategy 1: @dummyPokemon (most reliable — refreshed each navigation)
      if text.nil? && instance_variable_defined?(:@dummyPokemon)
        dummy = @dummyPokemon
        if dummy
          [:pokedex_entry, :description, :real_description, :dex_entry,
           :real_pokedex_entry, :entry_text, :dexentry].each do |attr|
            next unless dummy.respond_to?(attr)
            begin
              val = dummy.send(attr)
              if val.is_a?(String) && val.strip.length > 10
                text = val
                PokedexVoiceOver.log("  Displayed text found via @dummyPokemon.#{attr}")
                break
              end
            rescue StandardError => e
              PokedexVoiceOver.log("  @dummyPokemon.#{attr} failed: #{e.message}")
            end
          end
        end
      end

      # Strategy 2: KIF-specific scene methods (use current internal state)
      # These methods require arguments (species ID or head/body pair).
      # We detect the current species first, then try each method with the
      # appropriate arguments, falling back to fewer args on ArgumentError.
      if text.nil?
        kif_head, kif_fused = pokedex_vo_current_species
        nb = PokedexVoiceOver.nb_pokemon
        kif_composite = nil
        if kif_head && kif_fused && nb && nb > 0 && kif_head.is_a?(Integer) && kif_fused.is_a?(Integer)
          kif_composite = kif_head * nb + kif_fused
        end

        # Helper to extract a usable string from a method return value
        extract_text = lambda do |val|
          if val.is_a?(String) && val.strip.length > 10
            val
          elsif val.is_a?(Array)
            val.find { |v| v.is_a?(String) && v.strip.length > 10 }
          else
            nil
          end
        end

        # Try each method with progressively fewer arguments until one works
        [:getCustomDexEntry, :getCustomEntryText, :getAIDexEntry].each do |method_name|
          next unless respond_to?(method_name, true)
          val = nil
          called_with = nil

          # Build a list of argument sets to try (most specific first)
          arg_sets = []
          if method_name == :getAIDexEntry
            # getAIDexEntry expects (head, body) for fusions
            arg_sets << [kif_head, kif_fused] if kif_head && kif_fused
            arg_sets << [kif_composite] if kif_composite
            arg_sets << [kif_head] if kif_head
          else
            # getCustomDexEntry / getCustomEntryText expect (species)
            arg_sets << [kif_composite] if kif_composite
            arg_sets << [kif_head] if kif_head
          end
          # Always try with zero args as a last resort
          arg_sets << []

          arg_sets.each do |args|
            begin
              val = send(method_name, *args)
              called_with = args
              break
            rescue ArgumentError => e
              PokedexVoiceOver.log("  #{method_name}(#{args.map(&:inspect).join(', ')}) arg error: #{e.message}")
              val = nil
            rescue StandardError => e
              PokedexVoiceOver.log("  #{method_name}(#{args.map(&:inspect).join(', ')}) failed: #{e.message}")
              val = nil
              break  # non-argument errors are unlikely to be fixed by fewer args
            end
          end

          result = extract_text.call(val)
          if result
            text = result
            PokedexVoiceOver.log("  Displayed text found via #{method_name}(#{called_with&.map(&:inspect)&.join(', ')})")
            break
          end
        end
      end

      # Strategy 3: Check common instance variable names for entry text
      if text.nil?
        [:@dexEntry, :@entryText, :@description, :@pokemonEntry,
         :@dex_entry, :@entry_text, :@fusionEntry, :@fusion_entry,
         :@desc, :@dexentry, :@entrytext, :@pokedex_entry,
         :@entry, :@dex_text, :@pokemon_entry].each do |var|
          if instance_variable_defined?(var)
            val = instance_variable_get(var)
            if val.is_a?(String) && val.strip.length > 10
              text = val
              PokedexVoiceOver.log("  Displayed text found in #{var}")
              break
            end
          end
        end
      end

      # Strategy 4: Check @sprites hash for text overlay windows
      # KIF stores Pokédex text in sprite overlay windows; the displayed entry
      # text is often in a Window with a .text property.
      if text.nil? && instance_variable_defined?(:@sprites) && @sprites.is_a?(Hash)
        @sprites.each do |key, sprite|
          next unless sprite
          # Try .text property (common for Window_UnformattedTextPokemon etc.)
          [:text, :getText].each do |text_method|
            next unless sprite.respond_to?(text_method)
            begin
              val = sprite.send(text_method)
              if val.is_a?(String) && val.strip.length > 10
                text = val
                PokedexVoiceOver.log("  Displayed text found in @sprites[#{key.inspect}].#{text_method}")
                break
              end
            rescue StandardError
              next
            end
          end
          break if text
        end
      end

      # Strategy 5: Try GameData for both regular and fused species
      # For fusions, cross-check the composite result against the head
      # species' base entry text — if they match, the composite lookup
      # returned stale/wrong data (the head species text, not the fusion's).
      if text.nil? && defined?(GameData::Species)
        head, fused = pokedex_vo_current_species

        # Pre-fetch head species' base text for cross-checking fusions
        head_base_text = nil
        if head && fused
          begin
            head_data = GameData::Species.get(head)
            if head_data.respond_to?(:real_description)
              head_base_text = head_data.real_description
            elsif head_data.respond_to?(:pokedex_entry)
              head_base_text = head_data.pokedex_entry
            end
          rescue StandardError
            # ignore — head_base_text remains nil
          end
        end

        # For fusions, try the composite species ID first — KIF registers
        # fusion entries under the composite key in GameData::Species.
        if head && fused
          nb = PokedexVoiceOver.nb_pokemon
          composite = nil
          if nb && nb > 0 && head.is_a?(Integer) && fused.is_a?(Integer)
            composite = head * nb + fused
          end
          if composite
            begin
              data = GameData::Species.get(composite)
              candidate = nil
              if data.respond_to?(:pokedex_entry)
                val = data.pokedex_entry
                candidate = val if val.is_a?(String) && val.strip.length > 10
              end
              if candidate.nil? && data.respond_to?(:real_description)
                val = data.real_description
                candidate = val if val.is_a?(String) && val.strip.length > 10
              end
              # Discard if the composite text is identical to the head
              # species' base entry — this means GameData returned the
              # base species' text rather than the fusion's custom entry.
              if candidate && head_base_text
                norm_candidate = PokedexVoiceOver._normalize_text(candidate)
                norm_head = PokedexVoiceOver._normalize_text(head_base_text)
                if norm_candidate == norm_head
                  PokedexVoiceOver.log("  GameData composite #{composite} text matches head species base entry — discarding as stale")
                  candidate = nil
                end
              end
              if candidate
                text = candidate
                PokedexVoiceOver.log("  Displayed text found via GameData (composite #{composite})")
              end
            rescue StandardError => e
              PokedexVoiceOver.log("  GameData composite lookup failed: #{e.message}")
            end
          end
        end

        # For regular species, or if fusion composite lookup failed
        if text.nil? && head
          begin
            data = GameData::Species.get(head)
            if data.respond_to?(:real_description)
              text = data.real_description
            elsif data.respond_to?(:pokedex_entry)
              text = data.pokedex_entry
            end
            PokedexVoiceOver.log("  Displayed text found via GameData") if text
          rescue StandardError => e
            PokedexVoiceOver.log("  GameData text lookup failed: #{e.message}")
          end
        end
      end

      # Strategy 6: Try pbGetMessage for both regular and fused species
      if text.nil?
        head, fused = pokedex_vo_current_species
        if defined?(MessageTypes) && defined?(MessageTypes::Entries)
          begin
            # For fusions, try the composite ID first
            if head && fused
              nb = PokedexVoiceOver.nb_pokemon
              if nb && nb > 0 && head.is_a?(Integer) && fused.is_a?(Integer)
                composite = head * nb + fused
                val = pbGetMessage(MessageTypes::Entries, composite) rescue nil
                if val.is_a?(String) && !val.strip.empty? && val.strip.length > 10
                  text = val
                  PokedexVoiceOver.log("  Displayed text found via pbGetMessage (composite #{composite})")
                end
              end
            end
            # For regular species, or if fusion pbGetMessage failed
            if text.nil? && head
              sp_data = GameData::Species.get(head) if defined?(GameData::Species)
              id_num = sp_data.id_number if sp_data && sp_data.respond_to?(:id_number)
              if id_num
                text = pbGetMessage(MessageTypes::Entries, id_num) rescue nil
                PokedexVoiceOver.log("  Displayed text found via pbGetMessage") if text && !text.empty?
                text = nil if text && text.empty?
              end
            end
          rescue StandardError => e
            PokedexVoiceOver.log("  pbGetMessage text lookup failed: #{e.message}")
          end
        end
      end

      # Log all long string ivars for debugging (helps identify where KIF stores fusion text)
      if text.nil?
        PokedexVoiceOver.log("  Could not find displayed text — logging string ivars for debugging:")
        instance_variables.sort.each do |var|
          next if var.to_s.start_with?("@dex_vo_")
          val = instance_variable_get(var) rescue next
          if val.is_a?(String) && val.strip.length > 20
            PokedexVoiceOver.log("    #{var} (#{val.length} chars): #{val[0, 80].inspect}")
          end
        end
      end

      text
    end

    # ------------------------------------------------------------------
    # pbStartScene  — play when the Pokédex entry scene first opens
    # ------------------------------------------------------------------
    if method_defined?(:pbStartScene)
      PokedexVoiceOver.log("  Aliasing pbStartScene")
      alias dex_vo_orig_pbStartScene pbStartScene

      def pbStartScene(*args)
        dex_vo_orig_pbStartScene(*args)

        # NOTE: @page is nil (not an error) if the original method hasn't
        # set it yet.  In Ruby, accessing an undefined ivar returns nil —
        # it does NOT raise, so `rescue` never triggers.  We treat nil as
        # "we're on the entry page" because pbStartScene always opens to
        # the default page.
        page = @page
        PokedexVoiceOver.log("pbStartScene fired — @page=#{page.inspect}, entry_page=#{POKEDEX_VO_ENTRY_PAGE}")

        if page.nil? || page == POKEDEX_VO_ENTRY_PAGE
          head, fused = pokedex_vo_current_species
          @dex_vo_last_species = [head, fused]
          text = pokedex_vo_displayed_text
          PokedexVoiceOver.play(head, fused, text)
          # Signal to drawPage that we already played for this species,
          # preventing double-play when drawPage fires right after us.
          @dex_vo_scene_played = true
        else
          PokedexVoiceOver.log("  Skipping — page #{page} is not the entry page (#{POKEDEX_VO_ENTRY_PAGE})")
          PokedexVoiceOver.log("  Hint: if the voice should play here, set POKEDEX_VO_ENTRY_PAGE = #{page}")
        end
      end
    else
      PokedexVoiceOver.log("  WARNING: pbStartScene is NOT defined — cannot hook scene open")
    end

    # ------------------------------------------------------------------
    # pbShowPage  — play (optionally) when returning to the entry page
    # ------------------------------------------------------------------
    if method_defined?(:pbShowPage)
      PokedexVoiceOver.log("  Aliasing pbShowPage")
      alias dex_vo_orig_pbShowPage pbShowPage

      def pbShowPage(page, *args)
        prev_page = @page  # nil if not yet set (see note in pbStartScene)
        dex_vo_orig_pbShowPage(page, *args)

        PokedexVoiceOver.log("pbShowPage fired — page=#{page.inspect}, prev=#{prev_page.inspect}, play_on_change=#{PokedexVoiceOver.play_on_page_change?}")

        if page == POKEDEX_VO_ENTRY_PAGE
          head, fused = pokedex_vo_current_species
          current_species = [head, fused]
          species_changed = current_species != @dex_vo_last_species
          page_changed = prev_page != POKEDEX_VO_ENTRY_PAGE

          # Check and clear the navigation/scene played flags to prevent
          # double-play when pbGoToNext/pbGoToPrevious or pbStartScene
          # already played for this species.
          if @dex_vo_nav_played || @dex_vo_scene_played
            PokedexVoiceOver.log("  pbShowPage: skipping — already played by #{@dex_vo_nav_played ? 'navigation' : 'pbStartScene'} hook")
            @dex_vo_last_species = current_species
            @dex_vo_nav_played = false
            @dex_vo_scene_played = false
          elsif species_changed
            PokedexVoiceOver.log("  pbShowPage: species changed — playing")
            @dex_vo_last_species = current_species
            text = pokedex_vo_displayed_text
            PokedexVoiceOver.play(head, fused, text)
          elsif page_changed && PokedexVoiceOver.play_on_page_change?
            text = pokedex_vo_displayed_text
            PokedexVoiceOver.play(head, fused, text)
          end
        end
      end
    else
      PokedexVoiceOver.log("  pbShowPage is NOT defined — trying drawPage as fallback")

      # KIF uses drawPage instead of pbShowPage for page rendering.
      # Hook it so the "play_on_page_change" feature still works.
      if method_defined?(:drawPage)
        PokedexVoiceOver.log("  Aliasing drawPage (fallback for pbShowPage)")
        alias dex_vo_orig_drawPage drawPage

        def drawPage(page, *args)
          prev_page = @page  # capture before the original method updates it
          dex_vo_orig_drawPage(page, *args)

          PokedexVoiceOver.log("drawPage fired — page=#{page.inspect}, prev=#{prev_page.inspect}, play_on_change=#{PokedexVoiceOver.play_on_page_change?}")

          if page == POKEDEX_VO_ENTRY_PAGE
            head, fused = pokedex_vo_current_species
            current_species = [head, fused]
            species_changed = current_species != @dex_vo_last_species
            page_changed = prev_page != POKEDEX_VO_ENTRY_PAGE

            # Check and clear the navigation/scene played flags to prevent
            # double-play when pbGoToNext/pbGoToPrevious or pbStartScene
            # already played for this species.
            if @dex_vo_nav_played || @dex_vo_scene_played
              PokedexVoiceOver.log("  drawPage: skipping — already played by #{@dex_vo_nav_played ? 'navigation' : 'pbStartScene'} hook")
              @dex_vo_last_species = current_species
              @dex_vo_nav_played = false
              @dex_vo_scene_played = false
            elsif species_changed
              PokedexVoiceOver.log("  drawPage: species changed (#{@dex_vo_last_species.inspect} -> #{current_species.inspect}) — playing")
              @dex_vo_last_species = current_species
              text = pokedex_vo_displayed_text
              PokedexVoiceOver.play(head, fused, text)
            elsif page_changed && PokedexVoiceOver.play_on_page_change?
              PokedexVoiceOver.log("  drawPage: page changed to entry page — replaying")
              text = pokedex_vo_displayed_text
              PokedexVoiceOver.play(head, fused, text)
            end
          end
        end
      else
        PokedexVoiceOver.log("  WARNING: Neither pbShowPage nor drawPage defined — cannot hook page changes")
      end
    end

    # ------------------------------------------------------------------
    # pbEndScene  — stop the voice when the scene closes
    # ------------------------------------------------------------------
    if method_defined?(:pbEndScene)
      PokedexVoiceOver.log("  Aliasing pbEndScene")
      alias dex_vo_orig_pbEndScene pbEndScene

      def pbEndScene(*args)
        PokedexVoiceOver.log("pbEndScene fired")
        PokedexVoiceOver.stop
        dex_vo_orig_pbEndScene(*args)
      end
    else
      PokedexVoiceOver.log("  WARNING: pbEndScene is NOT defined — cannot hook scene close")
    end

    # ------------------------------------------------------------------
    # pbGoToNext / pbGoToPrevious — play voice when scrolling between
    # Pokémon entries in the Pokédex info scene
    # ------------------------------------------------------------------
    if method_defined?(:pbGoToNext)
      PokedexVoiceOver.log("  Aliasing pbGoToNext")
      alias dex_vo_orig_pbGoToNext pbGoToNext

      def pbGoToNext(*args)
        prev_species = @dex_vo_last_species
        dex_vo_orig_pbGoToNext(*args)

        PokedexVoiceOver.log("pbGoToNext fired — @page=#{@page.inspect}, prev_species=#{prev_species.inspect}")

        head, fused = pokedex_vo_current_species
        @dex_vo_last_species = [head, fused]
        if head == prev_species&.first && fused == prev_species&.last
          PokedexVoiceOver.log("  WARNING: species unchanged after pbGoToNext — possible stale state")
        end
        text = pokedex_vo_displayed_text
        PokedexVoiceOver.play(head, fused, text)
        # Signal to drawPage/pbShowPage that we already played for this
        # species, preventing the double-play that occurs when the page
        # rendering hook fires immediately after navigation.
        @dex_vo_nav_played = true
      end
    else
      PokedexVoiceOver.log("  WARNING: pbGoToNext is NOT defined — cannot hook next-entry navigation")
    end

    if method_defined?(:pbGoToPrevious)
      PokedexVoiceOver.log("  Aliasing pbGoToPrevious")
      alias dex_vo_orig_pbGoToPrevious pbGoToPrevious

      def pbGoToPrevious(*args)
        prev_species = @dex_vo_last_species
        dex_vo_orig_pbGoToPrevious(*args)

        PokedexVoiceOver.log("pbGoToPrevious fired — @page=#{@page.inspect}, prev_species=#{prev_species.inspect}")

        head, fused = pokedex_vo_current_species
        @dex_vo_last_species = [head, fused]
        if head == prev_species&.first && fused == prev_species&.last
          PokedexVoiceOver.log("  WARNING: species unchanged after pbGoToPrevious — possible stale state")
        end
        text = pokedex_vo_displayed_text
        PokedexVoiceOver.play(head, fused, text)
        # Signal to drawPage/pbShowPage that we already played for this
        # species, preventing the double-play that occurs when the page
        # rendering hook fires immediately after navigation.
        @dex_vo_nav_played = true
      end
    else
      PokedexVoiceOver.log("  WARNING: pbGoToPrevious is NOT defined — cannot hook previous-entry navigation")
    end
  end

  PokedexVoiceOver.log("Hook installation complete")
else
  PokedexVoiceOver.log("ERROR: PokemonPokedexInfo_Scene is NOT defined!")
  PokedexVoiceOver.log("  The mod cannot hook into the Pokédex scene.")
  PokedexVoiceOver.log("  This usually means the mod loaded BEFORE the game's Pokédex scripts.")
  # Try to discover any Pokédex-related classes so we can suggest the right name
  begin
    pokedex_classes = ObjectSpace.each_object(Class).select do |c|
      c.name.to_s =~ /pokedex|dex.*info|dex.*scene/i rescue false
    end
    if pokedex_classes.any?
      PokedexVoiceOver.log("  Pokédex-related classes found: #{pokedex_classes.map(&:name).sort.inspect}")
    else
      PokedexVoiceOver.log("  No Pokédex-related classes found — the class may load later")
    end
  rescue StandardError => e
    PokedexVoiceOver.log("  ObjectSpace scan failed: #{e.message}")
  end
end

