  # Convert a species value (Symbol, Integer, or String) to an UPPER_CASE
  # internal-name string suitable for use in a filename.
  def self.species_str(species)
    return nil if species.nil?
    # Integer 0 means "no species" in older Essentials versions
    return nil if species.is_a?(Integer) && species <= 0

    if species.is_a?(Symbol)
      return species.to_s.upcase
    end

    if species.is_a?(Integer)
      # Try to resolve via GameData (Essentials v19+)
      begin
        return GameData::Species.get(species).id.to_s.upcase
      rescue StandardError
        nil
      end
      # Fallback: try PBSpecies constant name (Essentials v18)
      begin
        name = getConstantName(PBSpecies, species)
        return name.upcase if name
      rescue StandardError
        nil
      end
      # Last resort: iterate GameData looking for matching id_number
      begin
        if defined?(GameData::Species)
          GameData::Species.each do |sp|
            if sp.respond_to?(:id_number) && sp.id_number == species
              log("  species_str: resolved #{species} to #{sp.id.inspect} via id_number scan")
              return sp.id.to_s.upcase
            end
          end
        end
      rescue StandardError
        nil
      end
      log("  WARNING: species_str falling back to raw integer #{species} — no name resolution succeeded")
      return species.to_s
    end

    return species.to_s.upcase
  end

  # Upper bound for "regular" (non-fusion) species IDs when scanning GameData.
  # Fusion composite IDs (head * NB_POKEMON + body) are well above this.
  MAX_BASE_SPECIES_ID = 10_000

  # -------------------------------------------------------------------------
  # NB_POKEMON helper — cached lookup with multiple fallbacks
  # -------------------------------------------------------------------------

  def self.nb_pokemon
    return @nb_pokemon if @nb_pokemon
    nb = nil
    nb = NB_POKEMON if defined?(NB_POKEMON)
    nb ||= (Settings::NB_POKEMON rescue nil) if defined?(Settings)
    nb ||= (Pokemon::NB_POKEMON rescue nil) if defined?(Pokemon)
    nb ||= (PBSpecies.maxValue rescue nil) if defined?(PBSpecies)
    # Fallback: count GameData species entries (non-fusion max ID)
    if nb.nil? && defined?(GameData::Species)
      begin
        max_id = 0
        GameData::Species.each do |sp|
          n = sp.id_number if sp.respond_to?(:id_number)
          max_id = n if n.is_a?(Integer) && n > max_id && n < MAX_BASE_SPECIES_ID
        end
        nb = max_id if max_id > 0
        log("  nb_pokemon via GameData scan: #{nb}") if nb
      rescue StandardError => e
        log("  nb_pokemon GameData scan failed: #{e.message}")
      end
    end
    @nb_pokemon = nb if nb
    nb
  end

