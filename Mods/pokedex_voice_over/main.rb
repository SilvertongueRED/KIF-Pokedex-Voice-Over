# =============================================================================
# Pokédex Voice Over — KIF Mod
# =============================================================================
# Reads Pokédex entries aloud in the style of Dexter, the classic Pokémon
# anime Pokédex narrator, whenever a Pokédex entry page is opened.
#
# Supports both regular Pokémon and every fused variant.
#
# AUDIO FILES
# -----------
# Audio files must be placed in:
#   <game root>/Audio/SE/Pokedex/
#
# Naming convention:
#   dex_SPECIES.ogg              — regular Pokémon  (e.g. dex_BULBASAUR.ogg)
#   dex_SPECIES1_SPECIES2.ogg    — fused Pokémon    (e.g. dex_BULBASAUR_CHARMANDER.ogg)
#
# A duration manifest generated by tools/generate_voices.py is read from:
#   <game root>/Audio/SE/Pokedex/dex_durations.json
# This is used to time sequential playback for fusions without a custom audio
# file (the mod plays each base species entry back-to-back).
#
# An entry text map generated by tools/generate_voices.py is read from:
#   <game root>/Audio/SE/Pokedex/dex_entry_map.json
# This maps each audio file to the Pokédex entry text it was generated from,
# allowing the mod to play the correct variant when multiple entries exist.
#
# Run  tools/generate_voices.py  from the repository to generate these files
# from your game's PBS data.
#
# DIAGNOSTIC LOG
# --------------
# The mod writes a diagnostic log to:
#   <game root>/Mods/pokedex_voice_over/debug.log
#
# Check this file if audio does not play — it records every hook call,
# species detection attempt, file-existence check, and playback call so
# you can see exactly where the process stops.
# =============================================================================

module PokedexVoiceOver
  MOD_ID = "pokedex_voice_over"

  # Subfolder inside Audio/SE/ where all Pokédex voice-over audio lives.
  AUDIO_SUBDIR = "Pokedex"

  # Fallback playback duration (seconds) used when dex_durations.json has no
  # entry for a given file.  5 seconds covers most Pokédex entries comfortably.
  SEQUENTIAL_DURATION_FALLBACK = 5.0

  # Delay (seconds) before voice-over playback begins, allowing the Pokémon's
  # cry to finish before the Pokédex entry is read aloud.
  CRY_DELAY = 0.5

  # Diagnostic log file — always written so users can report issues.
  LOG_FILE = "Mods/pokedex_voice_over/debug.log"

  # -------------------------------------------------------------------------
  # Diagnostic logging — writes to LOG_FILE on every significant event
  # -------------------------------------------------------------------------

  def self.log(msg)
    line = "[#{Time.now.strftime('%H:%M:%S')}] #{msg}"
    File.open(LOG_FILE, "a") { |f| f.puts(line) } rescue nil
  end

  # -------------------------------------------------------------------------
  # Settings helpers
  # -------------------------------------------------------------------------

  def self.settings
    ($mod_manager_settings || {})[MOD_ID] || {}
  end

  def self.enabled?
    if defined?(ModSettingsMenu)
      val = ModSettingsMenu.get(:pokedex_vo_enabled)
      return val == 1 if !val.nil?
    end
    settings.fetch("enabled", true)
  end

  def self.volume
    if defined?(ModSettingsMenu)
      val = ModSettingsMenu.get(:pokedex_vo_volume)
      return val.to_i.clamp(0, 100) if !val.nil?
    end
    settings.fetch("volume", 80).to_i.clamp(0, 100)
  end

  def self.play_on_page_change?
    if defined?(ModSettingsMenu)
      val = ModSettingsMenu.get(:pokedex_vo_replay_on_page_change)
      return val == 1 if !val.nil?
    end
    settings.fetch("play_on_page_change", false)
  end

  def self.mute_music?
    if defined?(ModSettingsMenu)
      val = ModSettingsMenu.get(:pokedex_vo_mute_music)
      return val == 1 if !val.nil?
    end
    settings.fetch("mute_music", true)
  end

  # -------------------------------------------------------------------------
  # Duration manifest (loaded lazily, cached)
  # -------------------------------------------------------------------------

  def self.durations
    return @durations if @durations
    path = "Audio/SE/#{AUDIO_SUBDIR}/dex_durations.json"
    @durations = begin
      if FileTest.exist?(path)
        require "json"
        JSON.parse(File.read(path))
      else
        {}
      end
    rescue StandardError
      {}
    end
  end

  # -------------------------------------------------------------------------
  # Entry map — maps audio file stems to their Pokédex entry texts
  # (loaded lazily, cached).  Used to match the displayed text to the
  # correct audio variant instead of picking one at random.
  # -------------------------------------------------------------------------

  def self.entry_map
    return @entry_map if @entry_map
    path = "Audio/SE/#{AUDIO_SUBDIR}/dex_entry_map.json"
    @entry_map = begin
      if FileTest.exist?(path)
        require "json"
        data = JSON.parse(File.read(path))
        log("  Loaded entry map: #{data.size} entries from #{path}")
        data
      else
        log("  Entry map not found: #{path}")
        {}
      end
    rescue StandardError => e
      log("  Entry map load error: #{e.message}")
      {}
    end
  end

  # -------------------------------------------------------------------------
  # BGM mute / restore helpers
  # -------------------------------------------------------------------------

  # Save the currently playing BGM and fade it out.
  # Called when the Pokédex scene opens so the voice-over (and the browsing
  # experience in general) is not drowned out by background music.
  # Does nothing when the mute_music setting is disabled.
  def self.save_and_mute_bgm
    return unless mute_music?
    return if @bgm_muted  # already muted — don't overwrite the saved BGM
    begin
      @saved_bgm = $game_system.playing_bgm.clone rescue nil
      Audio.bgm_fade(500) rescue nil
      @bgm_muted = true
      log("  BGM muted (saved: #{@saved_bgm.inspect})")
    rescue => e
      log("  BGM mute failed: #{e.message}")
      @bgm_muted = false
    end
  end

  # Restore the previously saved BGM.  Safe to call multiple times — does
  # nothing if no BGM was muted.
  def self.restore_bgm
    return unless @bgm_muted
    begin
      if @saved_bgm
        begin
          $game_system.bgm_play(@saved_bgm)
        rescue
          Audio.bgm_play(@saved_bgm.name, @saved_bgm.volume, @saved_bgm.pitch) rescue nil
        end
        log("  BGM restored: #{@saved_bgm.inspect}")
      end
      @bgm_muted = false
      @saved_bgm = nil
    rescue => e
      log("  BGM restore failed: #{e.message}")
    end
  end

  # -------------------------------------------------------------------------
  # Species helpers
  # -------------------------------------------------------------------------

  # Convert a species value (Symbol, Integer, or String) to an UPPER_CASE
  # internal-name string suitable for use in a filename.
  def self.species_str(species)
    return nil if species.nil?
    # Integer 0 means "no species" in older Essentials versions
    return nil if species.is_a?(Integer) && species <= 0

    if species.is_a?(Symbol)
      return species.to_s.upcase
    end

    if species.is_a?(Integer)
      # Try to resolve via GameData (Essentials v19+)
      begin
        return GameData::Species.get(species).id.to_s.upcase
      rescue StandardError
        nil
      end
      # Fallback: try PBSpecies constant name (Essentials v18)
      begin
        name = getConstantName(PBSpecies, species)
        return name.upcase if name
      rescue StandardError
        nil
      end
      # Last resort: iterate GameData looking for matching id_number
      begin
        if defined?(GameData::Species)
          GameData::Species.each do |sp|
            if sp.respond_to?(:id_number) && sp.id_number == species
              log("  species_str: resolved #{species} to #{sp.id.inspect} via id_number scan")
              return sp.id.to_s.upcase
            end
          end
        end
      rescue StandardError
        nil
      end
      log("  WARNING: species_str falling back to raw integer #{species} — no name resolution succeeded")
      return species.to_s
    end

    return species.to_s.upcase
  end

  # Upper bound for "regular" (non-fusion) species IDs when scanning GameData.
  # Fusion composite IDs (head * NB_POKEMON + body) are well above this.
  MAX_BASE_SPECIES_ID = 10_000

  # -------------------------------------------------------------------------
  # NB_POKEMON helper — cached lookup with multiple fallbacks
  # -------------------------------------------------------------------------

  def self.nb_pokemon
    return @nb_pokemon if @nb_pokemon
    nb = nil
    nb = NB_POKEMON if defined?(NB_POKEMON)
    nb ||= (Settings::NB_POKEMON rescue nil) if defined?(Settings)
    nb ||= (Pokemon::NB_POKEMON rescue nil) if defined?(Pokemon)
    nb ||= (PBSpecies.maxValue rescue nil) if defined?(PBSpecies)
    # Fallback: count GameData species entries (non-fusion max ID)
    if nb.nil? && defined?(GameData::Species)
      begin
        max_id = 0
        GameData::Species.each do |sp|
          n = sp.id_number if sp.respond_to?(:id_number)
          max_id = n if n.is_a?(Integer) && n > max_id && n < MAX_BASE_SPECIES_ID
        end
        nb = max_id if max_id > 0
        log("  nb_pokemon via GameData scan: #{nb}") if nb
      rescue StandardError => e
        log("  nb_pokemon GameData scan failed: #{e.message}")
      end
    end
    @nb_pokemon = nb if nb
    nb
  end

  # -------------------------------------------------------------------------
  # Playback helpers
  # -------------------------------------------------------------------------

  # Return true if an audio file for *bare_name* (without Audio/SE/ prefix)
  # exists on disk.  FileTest.exist? uses the real filesystem path so we must
  # supply the full path including the subdirectory and extension.
  def self._audio_exists?(bare_name)
    [".ogg", ".wav", ".mp3"].any? do |ext|
      path = "Audio/SE/#{bare_name}#{ext}"
      found = FileTest.exist?(path)
      log("  _audio_exists? #{path} => #{found}")
      found
    end
  end

  # Find all variant audio files for a given base bare_name.
  # Returns a list of bare names (relative to Audio/SE/) including the primary
  # file and any variant files (e.g. dex_BULBASAUR_CHARMANDER_v2).
  #
  # Variant naming convention:
  #   Pokedex/dex_SPECIES.ogg          — primary
  #   Pokedex/dex_SPECIES_v2.ogg       — variant 2
  #   Pokedex/dex_SPECIES_v3.ogg       — variant 3
  #   Pokedex/dex_SP1_SP2_v2.ogg       — fusion variant 2
  def self._find_variants(bare_name)
    variants = []
    variants << bare_name if _audio_exists?(bare_name)

    # Check for _v2, _v3, ... up to a reasonable limit
    n = 2
    max_variants = 20
    loop do
      break if n > max_variants
      candidate = "#{bare_name}_v#{n}"
      if _audio_exists?(candidate)
        variants << candidate
        n += 1
      else
        break
      end
    end

    log("  _find_variants(#{bare_name}) => #{variants.length} file(s)")
    variants
  end

  # Normalize text for comparison: strip, downcase, collapse whitespace.
  def self._normalize_text(text)
    return "" unless text.is_a?(String)
    text.strip.downcase.gsub(/\s+/, " ")
  end

  # Given a list of variant bare names and the displayed entry text,
  # find the variant whose generated text matches the displayed text.
  # Returns the matching bare name, or nil if no match found.
  def self._match_variant(variants, displayed_text)
    return nil unless displayed_text.is_a?(String) && !displayed_text.strip.empty?
    return nil if entry_map.empty?

    norm_displayed = _normalize_text(displayed_text)
    return nil if norm_displayed.empty?
    log("  _match_variant: trying to match (#{norm_displayed.length} chars): #{norm_displayed[0, 60]}...")

    # Try exact normalized match
    variants.each do |variant_bare|
      stem = variant_bare.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
      map_text = entry_map[stem]
      next unless map_text
      if _normalize_text(map_text) == norm_displayed
        log("  _match_variant: exact match => #{variant_bare}")
        return variant_bare
      end
    end

    # Try prefix match (first 40 normalized characters)
    prefix_len = 40
    if norm_displayed.length >= prefix_len
      norm_prefix = norm_displayed[0, prefix_len]
      variants.each do |variant_bare|
        stem = variant_bare.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
        map_text = entry_map[stem]
        next unless map_text
        if _normalize_text(map_text).start_with?(norm_prefix)
          log("  _match_variant: prefix match => #{variant_bare}")
          return variant_bare
        end
      end
    end

    log("  _match_variant: no match found")
    nil
  end

  # Play a single audio file (bare_name is relative to Audio/SE/).
  # Stops any currently playing SE first.
  #
  # Tries the bare path first (standard RGSS resolves relative to Audio/SE/),
  # then falls back to the full "Audio/SE/" prefixed path in case the engine
  # needs it (some MKXP builds behave differently).
  def self._play_bare(bare_name)
    Audio.se_stop
    log("  _play_bare: Audio.se_play('#{bare_name}', #{volume}, 100)")
    begin
      Audio.se_play(bare_name, volume, 100)
      return
    rescue StandardError => e
      log("  _play_bare: bare path failed (#{e.class}: #{e.message})")
    end
    # Fallback: try with "Audio/SE/" prefix
    full = "Audio/SE/#{bare_name}"
    log("  _play_bare: retrying with Audio.se_play('#{full}', #{volume}, 100)")
    begin
      Audio.se_play(full, volume, 100)
    rescue StandardError => e
      log("  _play_bare: full path also failed (#{e.class}: #{e.message})")
    end
  end

  # -------------------------------------------------------------------------
  # Playback
  # -------------------------------------------------------------------------

  # Play the voice-over for *species* (and optionally its fusion partner).
  #
  # For fused Pokémon:
  #   1. If a dedicated fusion audio file exists, play it directly.
  #      If variant files exist (_v2, _v3…), the one matching the
  #      *displayed_text* is chosen when possible; falls back to random.
  #   2. Otherwise, if both base-species files exist, play them sequentially
  #      in a background thread (head first, then body after the head
  #      finishes — timed using the dex_durations.json manifest).
  #
  # Returns silently if no matching audio can be found.
  def self.play(species, fused_species = nil, displayed_text = nil)
    log("play() called — enabled=#{enabled?}, species=#{species.inspect}, fused=#{fused_species.inspect}, text=#{displayed_text ? displayed_text[0,40].inspect : 'nil'}")
    return unless enabled?

    base = species_str(species)
    log("  base species_str => #{base.inspect}")
    return unless base

    fused = species_str(fused_species)
    log("  fused species_str => #{fused.inspect}")

    # Advance the generation counter to invalidate any in-progress sequential
    # playback thread without forcibly killing it.
    @playback_gen = (@playback_gen || 0) + 1
    my_gen = @playback_gen  # snapshot so the thread can self-cancel

    # All playback goes through a background thread so the CRY_DELAY and any
    # sequential waits never freeze the game loop.
    begin
      Thread.new do
        begin
          # Wait for the Pokémon's cry to finish before reading the entry.
          _interruptible_sleep(CRY_DELAY, my_gen)
          next if @playback_gen != my_gen

          if fused
            fusion_bare = "#{AUDIO_SUBDIR}/dex_#{base}_#{fused}"

            # Check for the primary fusion file and any variants
            fusion_variants = _find_variants(fusion_bare)

            # Try reversed ordering (dex_BODY_HEAD) if forward not found.
            # KIF may encode fusions with head/body in either order depending
            # on version and how the composite ID is decomposed.
            reversed_bare = nil
            if fusion_variants.empty?
              reversed_bare = "#{AUDIO_SUBDIR}/dex_#{fused}_#{base}"
              fusion_variants = _find_variants(reversed_bare)
              if fusion_variants.any?
                log("  Using reversed fusion ordering: #{reversed_bare}")
                fusion_bare = reversed_bare
              end
            end

            if fusion_variants.any?
              matched = _match_variant(fusion_variants, displayed_text)
              chosen = matched || fusion_variants.sample
              log("  Playing fusion audio: #{chosen} (from #{fusion_variants.length} variant(s), matched=#{!matched.nil?})")
              _play_bare(chosen)
            else
              # No custom fusion entry — fall back to sequential base-species playback
              bare_head = "#{AUDIO_SUBDIR}/dex_#{base}"
              bare_body = "#{AUDIO_SUBDIR}/dex_#{fused}"

              if _audio_exists?(bare_head) && _audio_exists?(bare_body)
                log("  Playing sequential: #{bare_head} then #{bare_body}")
                head_duration = (durations["dex_#{base}.ogg"] || SEQUENTIAL_DURATION_FALLBACK).to_f
                Audio.se_stop
                Audio.se_play(bare_head, volume, 100)
                _interruptible_sleep(head_duration, my_gen)
                if @playback_gen == my_gen
                  Audio.se_play(bare_body, volume, 100)
                end
              else
                log("  No fusion or sequential audio available — skipping")
              end
            end
          else
            bare = "#{AUDIO_SUBDIR}/dex_#{base}"

            # Check for the primary file and any variants
            variants = _find_variants(bare)
            if variants.empty?
              log("  No audio file found — skipping")
              next
            end

            matched = _match_variant(variants, displayed_text)
            chosen = matched || variants.sample
            log("  Playing species audio: #{chosen} (from #{variants.length} variant(s), matched=#{!matched.nil?})")
            _play_bare(chosen)
          end
        rescue StandardError => e
          PokedexVoiceOver.log("  Playback thread error: #{e.class}: #{e.message}")
        end
      end
    rescue StandardError => e
      log("  Thread creation error: #{e.class}: #{e.message}")
    end
  end

  # Sleep in small increments so we respond promptly to new play/stop calls
  # (which bump @playback_gen).  Returns early if the generation changes.
  def self._interruptible_sleep(duration, gen)
    elapsed = 0.0
    step = 0.1
    while elapsed < duration
      break if @playback_gen != gen
      sleep(step)
      elapsed += step
    end
  end

  # Stop any playing voice-over, including any pending sequential playback.
  # Bumping @playback_gen causes any running playback thread to exit cleanly.
  # NOTE: BGM is now managed by the scene hooks (muted on open, restored on
  # close), so stop only handles SE playback.
  def self.stop
    @playback_gen = (@playback_gen || 0) + 1
    begin
      Audio.se_stop
    rescue StandardError => e
      log("stop error: #{e.class}: #{e.message}")
    end
  end
end

# =============================================================================
# Hook into PokemonPokedexInfo_Scene
# =============================================================================
# We alias three methods:
#   pbStartScene  — fires once when the Pokédex entry scene opens
#   pbShowPage    — fires whenever the displayed page changes
#   pbEndScene    — fires when the scene closes (so we can stop the audio)
#
# The description/entry page is typically page index 0 in Pokémon Infinite
# Fusion.  Adjust ENTRY_PAGE below if your build uses a different index.
#
# KNOWN BUG FIXES (v1.10.0)
# --------------------------
# - KIF scene methods (getCustomDexEntry, getCustomEntryText, getAIDexEntry)
#   now called with correct arguments (species/composite ID) instead of zero
#   args.  Uses arity-aware fallback to try multiple argument sets.
# - GameData composite lookup now cross-checks fusion text against the head
#   species' base entry; discards the result when they match (indicating the
#   lookup returned stale base-species text, not the fusion's custom entry).
# - New @sprites overlay text extraction strategy reads displayed entry text
#   from KIF's UI text windows when other methods fail.
# - pbGetMessage now also tried with the composite species ID for fusions.
# - Double playback eliminated: navigation hooks (pbGoToNext/pbGoToPrevious)
#   and pbStartScene set flags (@dex_vo_nav_played / @dex_vo_scene_played)
#   that drawPage/pbShowPage check and clear, preventing the second play()
#   call that previously occurred on every navigation or scene open.
#
# KNOWN BUG FIXES (v1.9.0)
# -------------------------
# - CRY_DELAY reduced from 1.0s to 0.5s — voice reads sooner after opening.
# - Fusion species detection: composite-ID decomposition now also tries
#   @dummyPokemon.id_number when .species returns a Symbol, fixing fusions
#   that returned the wrong species name or played the wrong audio file.
# - species_str: added id_number scan fallback when GameData::Species.get()
#   and PBSpecies both fail for an integer ID, with warning log when falling
#   back to raw number.
# - Scrolling voice: pbGoToNext/pbGoToPrevious no longer gate playback on
#   @page == ENTRY_PAGE (the page ivar could be stale after scrolling).
#   Voice now always triggers on navigation.
# - drawPage and pbShowPage hooks now detect species changes (not just page
#   changes), so voice plays reliably after scrolling even if pbGoToNext
#   fires with stale state or is bypassed by the engine.
#
# KNOWN BUG FIXES (v1.8.0)
# -------------------------
# - Species detection now prioritizes @dummyPokemon (refreshed by
#   pbUpdateDummyPokemon) over @species, which may be stale after scrolling
#   via pbGoToNext/pbGoToPrevious.  @dexlist[@index] is also checked before
#   the potentially-stale @species ivar.
# - Fusion audio lookup now tries both orderings (dex_HEAD_BODY and
#   dex_BODY_HEAD) since KIF may encode fusions either way depending on
#   version and composite-ID decomposition order.
# - Fusion partner is now extracted from @dummyPokemon attributes (.fused,
#   .fused_species, etc.) before falling back to separate ivars.
# - Text detection now prioritizes @dummyPokemon attributes and KIF scene
#   methods (getCustomDexEntry, etc.) over instance variable scanning, which
#   was often returning stale data after navigation.
# - Navigation state tracking: pbGoToNext/pbGoToPrevious now log when the
#   detected species hasn't changed (stale-state warning) to help diagnose
#   remaining issues.
# - Enhanced diagnostic logging shows @dummyPokemon capabilities, species
#   cross-checks, and all detection strategy results.
#
# KNOWN BUG FIXES (v1.7.0)
# -------------------------
# - Scrolling between Pokémon entries (pbGoToNext / pbGoToPrevious) now
#   triggers voice-over playback for the newly displayed entry.  Previously
#   only manually selecting a Pokémon via the action button would play.
# - Fusion Pokédex entry text detection now uses KIF-specific scene methods
#   (getCustomDexEntry, getCustomEntryText) and composite-ID GameData lookup,
#   fixing fusions that were not read aloud or played the wrong variant.
# - NB_POKEMON detection now uses a centralized helper with additional
#   fallbacks (Settings::NB_POKEMON, GameData species scan) so fusion
#   species decomposition works in more KIF builds.
# - @dummyPokemon is now checked as a text source for dex entry text.
#
# KNOWN BUG FIXES (v1.6.0)
# -------------------------
# - Variant audio selection now matches the displayed Pokédex entry text
#   instead of picking a random variant.  Uses dex_entry_map.json (generated
#   by tools/generate_voices.py) to look up which audio file corresponds to
#   the text the game is currently showing.  Falls back to random selection
#   when the displayed text cannot be determined or matched.
#
# KNOWN BUG FIXES (v1.5.0)
# -------------------------
# - BGM is now muted as soon as the Pokédex *menu* opens (PokemonPokedex_Scene)
#   rather than when opening an individual entry.  Music resumes only when
#   closing the Pokédex menu completely.
# - A 1-second delay is now applied before the voice-over plays, so the
#   Pokémon's cry is heard first before the Pokédex entry audio begins.
#
# KNOWN BUG FIXES (v1.4.0)
# -------------------------
# - Music is now muted as soon as the Pokédex scene opens (regardless of
#   which page is shown) and restored when the scene closes.  Previously
#   music was only muted when a voice-over played on the entry page.
# - Fused Pokémon with multiple Pokédex entries are now supported.  The mod
#   detects variant audio files (dex_SP1_SP2_v2.ogg, _v3.ogg, …) and
#   randomly selects one each time — matching the game's behaviour of
#   choosing a random entry for fusions.
#
# KNOWN BUG FIXES (v1.3.0)
# -------------------------
# - Fused Pokémon without custom dex entries now play correctly.  KIF encodes
#   fused species as a single integer (head_id * NB_POKEMON + body_id).
#   pokedex_vo_current_species now detects and decomposes these IDs before
#   falling back to separate fused-species ivar lookups.
# - BGM is automatically muted when voice-over starts and restored when it
#   stops (or the Pokédex is closed).  Controlled by the new mute_music setting.
# - All settings are now also registered in the Mod Settings menu (Interface
#   category) when Stonewall's Mod Settings mod is installed.
#
# KNOWN BUG FIXES (v1.2.0)
# -------------------------
# - POKEDEX_VO_ENTRY_PAGE was 0 but KIF uses 1-based page indices — the
#   entry/description page is page 1.  Changed default to 1.
# - pbShowPage does not exist in KIF — added a fallback hook on drawPage
#   so the "play_on_page_change" feature works in KIF builds.
#
# KNOWN BUG FIXES (v1.1.0)
# -------------------------
# - Instance-variable access (e.g. `@page rescue fallback`) does NOT raise
#   in Ruby — undefined ivars return nil silently.  Replaced with explicit
#   nil-checks so the fallback values are actually used.
# - Fusion-species fallback chain now uses instance_variable_defined? so
#   alternate variable names (@dexSpecies2, etc.) are actually tried.
# - Species detection now also checks @dexlist[@index] (common Essentials
#   pattern) as a fallback when @species is not set.
