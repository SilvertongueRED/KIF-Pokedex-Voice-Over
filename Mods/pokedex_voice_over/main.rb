# =============================================================================
# Pokédex Voice Over — KIF Mod
# =============================================================================
# Reads Pokédex entries aloud in the style of Dexter, the classic Pokémon
# anime Pokédex narrator, whenever a Pokédex entry page is opened.
#
# Supports both regular Pokémon and every fused variant.
#
# AUDIO FILES
# -----------
# Audio files must be placed in:
#   <game root>/Audio/SE/Pokedex/
#
# Naming convention:
#   dex_SPECIES.ogg              — regular Pokémon  (e.g. dex_BULBASAUR.ogg)
#   dex_SPECIES1_SPECIES2.ogg    — fused Pokémon    (e.g. dex_BULBASAUR_CHARMANDER.ogg)
#
# A duration manifest generated by tools/generate_voices.py is read from:
#   <game root>/Audio/SE/Pokedex/dex_durations.json
# This is used to time sequential playback for fusions without a custom audio
# file (the mod plays each base species entry back-to-back).
#
# An entry text map generated by tools/generate_voices.py is read from:
#   <game root>/Audio/SE/Pokedex/dex_entry_map.json
# This maps each audio file to the Pokédex entry text it was generated from,
# allowing the mod to play the correct variant when multiple entries exist.
#
# Run  tools/generate_voices.py  from the repository to generate these files
# from your game's PBS data.
#
# DIAGNOSTIC LOG
# --------------
# The mod writes a diagnostic log to:
#   <game root>/Mods/pokedex_voice_over/debug.log
#
# Check this file if audio does not play — it records every hook call,
# species detection attempt, file-existence check, and playback call so
# you can see exactly where the process stops.
# =============================================================================

# Try to load the json standard library.  MKXP-EX (the Ruby runtime used by
# KIF) does not ship the json gem, so we fall back to a tiny parser that
# handles the flat { "key": value } objects produced by generate_voices.py.
begin
  require "json"
rescue ScriptError, StandardError
  module JSON
    # Minimal JSON parser — supports flat objects whose values are strings,
    # numbers, booleans, or null.  This is sufficient for dex_durations.json
    # and dex_entry_map.json (both are single-level hashes produced by
    # Python's json.dump with indent=2).
    def self.parse(str)
      result = {}
      # Strip surrounding braces and iterate over "key": value pairs.
      inner = str.strip.sub(/\A\{/, "").sub(/\}\s*\z/, "")
      inner.scan(/"((?:[^"\\]|\\.)*)"\s*:\s*("(?:[^"\\]|\\.)*"|[-+]?\d+(?:\.\d+)?(?:[eE][-+]?\d+)?|true|false|null)/) do |key, val|
        key = _unescape(key)
        if val.start_with?('"')
          result[key] = _unescape(val[1..-2])
        elsif val == "true"
          result[key] = true
        elsif val == "false"
          result[key] = false
        elsif val == "null"
          result[key] = nil
        elsif val.include?(".") || val.include?("e") || val.include?("E")
          result[key] = val.to_f
        else
          result[key] = val.to_i
        end
      end
      result
    end

    def self._unescape(s)
      s.gsub(/\\(["\\\/bfnrt]|u[0-9a-fA-F]{4})/) do |m|
        case m
        when '\\"' then '"'
        when '\\\\' then '\\'
        when '\\/' then '/'
        when '\\b' then "\b"
        when '\\f' then "\f"
        when '\\n' then "\n"
        when '\\r' then "\r"
        when '\\t' then "\t"
        else
          # \\uXXXX
          [m[2..5].to_i(16)].pack("U")
        end
      end
    end
  end
end

module PokedexVoiceOver
  MOD_ID = "pokedex_voice_over"

  # Subfolder inside Audio/SE/ where all Pokédex voice-over audio lives.
  AUDIO_SUBDIR = "Pokedex"

  # Fallback playback duration (seconds) used when dex_durations.json has no
  # entry for a given file.  5 seconds covers most Pokédex entries comfortably.
  SEQUENTIAL_DURATION_FALLBACK = 5.0

  # Delay (seconds) before voice-over playback begins, allowing the Pokémon's
  # cry to finish before the Pokédex entry is read aloud.
  CRY_DELAY = 1.0

  # Sub-folder name (inside Audio/SE/Pokedex/) for TTS-generated cache
  # files (Fish-Speech or Piper).  Each unique entry text gets its own
  # deterministic filename so re-visits are instant.
  TTS_CACHE_SUBDIR = "tts_cache"

  # -------------------------------------------------------------------------
  # Fish-Speech sidecar (primary TTS backend)
  # -------------------------------------------------------------------------
  # Fish-Speech is an open-source voice-cloning TTS engine - the same engine
  # that powers fish.audio.  It runs as a small persistent HTTP server in the
  # fish_speech/ subfolder so the model stays loaded between requests (a
  # cold start takes ~10-30s; per-request inference is ~0.5s GPU / 3-8s CPU).
  #
  # We talk to it over plain HTTP on localhost via raw TCPSocket calls.
  # MKXP-Z's embedded Ruby does not ship net/http, so we use TCPSocket directly.
  FISH_SPEECH_HOST = "127.0.0.1"
  FISH_SPEECH_PORT = 7861
  FISH_SPEECH_DIR  = "Mods/pokedex_voice_over/fish_speech"

  # Do not re-spawn the sidecar more often than this (seconds).  The
  # autostart path is throttled rather than one-shot so the server can
  # self-heal after an idle-timeout/crash without launching a storm of
  # launcher windows while it is still loading.
  FISH_SPEECH_AUTOSTART_RETRY_SECS = 30.0
  # When the sidecar is known-DOWN, re-poll /health this often (seconds)
  # instead of caching the "down" verdict for the full positive-result
  # minute, so a freshly auto-restarted server is picked up promptly.
  FISH_SPEECH_DOWN_RECHECK_SECS = 5.0

  # How long (seconds) to wait for /tts to return a WAV.  Fish-Speech needs a
  # generous ceiling on slower CPUs - entries with several sentences can take
  # 10-30s.  After this we fall through to Piper.
  FISH_SPEECH_TIMEOUT = 45

  # Health-check timeout (seconds) - should be very short; the endpoint is
  # purely informational and returns instantly.
  FISH_SPEECH_HEALTH_TIMEOUT = 1

  # Diagnostic log file — always written so users can report issues.
  LOG_FILE = "Mods/pokedex_voice_over/debug.log"

  # -------------------------------------------------------------------------
  # Diagnostic logging — writes to LOG_FILE on every significant event
  # -------------------------------------------------------------------------

  def self.log(msg)
    line = "[#{Time.now.strftime('%H:%M:%S')}] #{msg}"
    File.open(LOG_FILE, "a") { |f| f.puts(line) } rescue nil
  end

