  # Return true if an audio file for *bare_name* (without Audio/SE/ prefix)
  # exists on disk.  FileTest.exist? uses the real filesystem path so we must
  # supply the full path including the subdirectory and extension.
  def self._audio_exists?(bare_name)
    [".ogg", ".wav", ".mp3"].any? do |ext|
      path = "Audio/SE/#{bare_name}#{ext}"
      found = FileTest.exist?(path)
      log("  _audio_exists? #{path} => #{found}")
      found
    end
  end

  # Find all variant audio files for a given base bare_name.
  # Returns a list of bare names (relative to Audio/SE/) including the primary
  # file and any variant files (e.g. dex_BULBASAUR_CHARMANDER_v2).
  #
  # Variant naming convention:
  #   Pokedex/dex_SPECIES.ogg          — primary
  #   Pokedex/dex_SPECIES_v2.ogg       — variant 2
  #   Pokedex/dex_SPECIES_v3.ogg       — variant 3
  #   Pokedex/dex_SP1_SP2_v2.ogg       — fusion variant 2
  def self._find_variants(bare_name)
    variants = []
    variants << bare_name if _audio_exists?(bare_name)

    # Check for _v2, _v3, ... up to a reasonable limit
    n = 2
    max_variants = 20
    loop do
      break if n > max_variants
      candidate = "#{bare_name}_v#{n}"
      if _audio_exists?(candidate)
        variants << candidate
        n += 1
      else
        break
      end
    end

    log("  _find_variants(#{bare_name}) => #{variants.length} file(s)")
    variants
  end

  # Normalize text for comparison: strip, downcase, remove all punctuation,
  # and collapse whitespace.  Both the displayed text from KIF and the
  # entry-map values go through this, so aggressively stripping punctuation
  # and whitespace differences ensures they converge to the same form.
  def self._normalize_text(text)
    return "" unless text.is_a?(String)
    text.strip.downcase.gsub(/[^a-z0-9\s]/, "").gsub(/\s+/, " ").strip
  end

  # Generate a portmanteau fusion name the way KIF does at runtime.
  # Takes roughly the first half of the head name (ceil) and the last half
  # of the body name (floor) and concatenates them.
  # Example: "Bulbasaur" + "Charmander" => "Bulbander"
  def self._make_fusion_name(head_name, body_name)
    head = head_name.to_s.strip
    body = body_name.to_s.strip
    return (head.empty? ? body : head) if head.empty? || body.empty?

    head = head[0].upcase + head[1..] if head.length > 0
    body = body[0].upcase + body[1..] if body.length > 0

    head_split = (head.length / 2.0).ceil
    body_split = body.length / 2

    head_part = head[0, head_split]
    body_part = body[body_split..]

    head_part + body_part.downcase
  end

  # Try to look up a custom fused name from Data/custom_fused_pokemon_names.tsv
  # (or .txt).  Returns the custom name string or nil if not found.
  # head_species and body_species should be species identifiers (Symbol, Integer, or String).
  def self._lookup_custom_fused_name(head_species, body_species)
    @custom_fused_names ||= _load_custom_fused_names
    return nil if @custom_fused_names.empty?

    head_str = species_str(head_species)
    body_str = species_str(body_species)
    return nil unless head_str && body_str

    @custom_fused_names["#{head_str.upcase}\t#{body_str.upcase}"]
  end

  # Load KIF's Data/custom_fused_pokemon_names.tsv (or .txt) into a lookup hash.
  # Keys are "HEAD_SPECIES\tBODY_SPECIES" (uppercase), values are custom names.
  # Uses nb_pokemon-based ID-to-species resolution via GameData when available.
  def self._load_custom_fused_names
    names = {}
    candidates = [
      "Data/custom_fused_pokemon_names.tsv",
      "Data/custom_fused_pokemon_names.txt"
    ]
    path = candidates.find { |p| File.exist?(p) rescue false }
    return names unless path

    log("Loading custom fused names from #{path}")
    begin
      File.foreach(path) do |line|
        line = line.strip
        next if line.empty? || line.start_with?("#")
        parts = line.split("\t")
        next if parts.length < 3
        head_num = parts[0].strip.to_i
        body_num = parts[1].strip.to_i
        fused_name = parts[2].strip
        next if fused_name.empty? || head_num <= 0 || body_num <= 0

        # Resolve dex numbers to species names via GameData if available
        head_name = nil
        body_name = nil
        begin
          if defined?(GameData::Species)
            hd = GameData::Species.try_get(head_num)
            bd = GameData::Species.try_get(body_num)
            head_name = hd.id.to_s.upcase if hd && hd.respond_to?(:id)
            body_name = bd.id.to_s.upcase if bd && bd.respond_to?(:id)
          end
        rescue StandardError
          # GameData not available — skip this entry
        end

        if head_name && body_name
          names["#{head_name}\t#{body_name}"] = fused_name
        end
      end
    rescue StandardError => e
      log("WARNING: Failed to load custom fused names: #{e.message}")
    end
    log("Loaded #{names.size} custom fused names")
    names
  end

  # Replace the literal POKENAME placeholder in entry text with the
  # fusion's actual fused name — using custom name data if available,
  # falling back to a portmanteau algorithm.
  # Returns the original text unchanged if POKENAME is not present.
  def self._resolve_pokename(text, head_species, body_species)
    return text unless text && text =~ /POKENAME/i
    head_name = species_str(head_species)
    body_name = species_str(body_species)
    return text unless head_name && body_name

    # 1. Try custom fused name from Data/custom_fused_pokemon_names.tsv
    display_name = _lookup_custom_fused_name(head_species, body_species)
    if display_name
      log("  POKENAME resolved via custom name: #{head_name} + #{body_name} => #{display_name}")
    else
      # 2. Fall back to portmanteau algorithm
      display_name = _make_fusion_name(head_name, body_name)
      log("  POKENAME resolved via portmanteau: #{head_name} + #{body_name} => #{display_name}")
    end

    text.gsub(/POKENAME/i, display_name)
  end

  # Given a list of variant bare names and the displayed entry text,
  # find the variant whose generated text matches the displayed text.
  # Returns the matching bare name, or nil if no match found.
  #
  # Optional head_species / body_species are used to resolve POKENAME
  # placeholders in entry map text when normal matching fails.
  def self._match_variant(variants, displayed_text, head_species = nil, body_species = nil)
    return nil unless displayed_text.is_a?(String) && !displayed_text.strip.empty?
    return nil if entry_map.empty?

    norm_displayed = _normalize_text(displayed_text)
    return nil if norm_displayed.empty?
    log("  _match_variant: trying to match (#{norm_displayed.length} chars): #{norm_displayed[0, 60]}...")

    # --- helper: run all matching strategies against a given text resolver ---
    # resolver is a lambda(map_text) that returns the text to compare against.
    _try_match = lambda do |resolver, tag|
      # Try exact normalized match
      variants.each do |variant_bare|
        stem = variant_bare.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
        map_text = resolver.call(entry_map[stem])
        next unless map_text
        if _normalize_text(map_text) == norm_displayed
          log("  _match_variant: #{tag}exact match => #{variant_bare}")
          return variant_bare
        end
      end

      # Try prefix match (first 40 normalized characters)
      prefix_len = 40
      if norm_displayed.length >= prefix_len
        norm_prefix = norm_displayed[0, prefix_len]
        variants.each do |variant_bare|
          stem = variant_bare.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
          map_text = resolver.call(entry_map[stem])
          next unless map_text
          if _normalize_text(map_text).start_with?(norm_prefix)
            log("  _match_variant: #{tag}prefix match => #{variant_bare}")
            return variant_bare
          end
        end
      end

      # Try substring/contains match
      variants.each do |variant_bare|
        stem = variant_bare.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
        map_text = resolver.call(entry_map[stem])
        next unless map_text
        norm_map = _normalize_text(map_text)
        next if norm_map.empty?
        if norm_map.include?(norm_displayed) || norm_displayed.include?(norm_map)
          log("  _match_variant: #{tag}substring match => #{variant_bare}")
          return variant_bare
        end
      end

      # Word-overlap fallback
      displayed_words = norm_displayed.split(" ").select { |w| w.length >= 4 }.uniq
      if displayed_words.length >= 2
        best_variant = nil
        best_ratio = 0.0
        variants.each do |variant_bare|
          stem = variant_bare.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
          map_text = resolver.call(entry_map[stem])
          next unless map_text
          norm_map = _normalize_text(map_text)
          map_words = norm_map.split(" ").select { |w| w.length >= 4 }.uniq
          next if map_words.empty?
          common = (displayed_words & map_words).length
          ratio = common.to_f / [displayed_words.length, map_words.length].max
          if ratio > best_ratio
            best_ratio = ratio
            best_variant = variant_bare
          end
        end
        if best_variant && best_ratio >= 0.35
          log("  _match_variant: #{tag}word-overlap match (ratio=#{best_ratio.round(2)}) => #{best_variant}")
          return best_variant
        end
      end

      # Fuzzy LCS fallback
      alpha_displayed = norm_displayed.gsub(/[^a-z0-9]/, "")
      if alpha_displayed.length >= 20
        best_variant = nil
        best_ratio = 0.0
        variants.each do |variant_bare|
          stem = variant_bare.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
          map_text = resolver.call(entry_map[stem])
          next unless map_text
          alpha_map = _normalize_text(map_text).gsub(/[^a-z0-9]/, "")
          next if alpha_map.empty?
          ratio = _lcs_ratio(alpha_displayed, alpha_map)
          if ratio > best_ratio
            best_ratio = ratio
            best_variant = variant_bare
          end
        end
        if best_variant && best_ratio >= 0.85
          log("  _match_variant: #{tag}fuzzy LCS match (ratio=#{best_ratio.round(2)}) => #{best_variant}")
          return best_variant
        end
      end

      nil  # no match in this pass
    end

    # --- Pass 1: try matching with raw entry map text -----------------------
    identity = lambda { |t| t }
    result = _try_match.call(identity, "")
    return result if result

    # --- Pass 2: if any entry map value contains POKENAME and we have
    #     species context, retry all strategies after resolving it -----------
    if head_species && body_species
      has_pokename = variants.any? do |vb|
        stem = vb.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
        mt = entry_map[stem]
        mt && mt =~ /POKENAME/i
      end
      if has_pokename
        log("  _match_variant: retrying with POKENAME resolved (head=#{head_species}, body=#{body_species})")
        resolver = lambda { |t| t ? _resolve_pokename(t, head_species, body_species) : nil }
        result = _try_match.call(resolver, "POKENAME-resolved ")
        return result if result
      end

      # --- Pass 3: POKENAME portmanteau mismatch fallback -------------------
      # KIF's runtime portmanteau algorithm may differ from the mod's
      # _make_fusion_name (producing e.g. "Bulbagey" vs "PIDasaur").
      # When Pass 2 still fails, strip POKENAME from the entry_map text and
      # strip the first capitalized word from the displayed text — if the rest
      # of the content matches, we have a reliable hit despite the name diff.
      if has_pokename
        log("  _match_variant: retrying with POKENAME stripped (nameless comparison)")
        # Build a "nameless" version of the displayed text:
        # remove the first word that could be the fusion's display name —
        # a capitalized, all-letter word of at least 4 characters that is NOT
        # a common English / Pokédex structural word.
        common_words = %w[this that the a an is was are were has have had
                           its it of in on at to for and or but with by
                           from as when where which who what known called
                           such very only also still once even they them
                           their use uses using can could will would
                           should may might must able been being become]
        nameless_displayed = displayed_text.dup
        # Strip the first word that looks like a Pokémon name
        nameless_displayed = nameless_displayed.sub(/\b([A-Z][a-zA-Z]{3,})\b/) do |m|
          common_words.include?(m.downcase) ? m : ""
        end
        norm_nameless_displayed = _normalize_text(nameless_displayed)
        unless norm_nameless_displayed.empty?
          nameless_resolver = lambda do |t|
            next nil unless t
            # Strip POKENAME from entry_map text
            t.gsub(/POKENAME/i, "")
          end
          # Re-run _try_match using the nameless displayed text as the base
          _try_match_nameless = lambda do |resolver2, tag2|
            variants.each do |variant_bare|
              stem = variant_bare.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
              map_text = resolver2.call(entry_map[stem])
              next unless map_text
              if _normalize_text(map_text) == norm_nameless_displayed
                log("  _match_variant: #{tag2}nameless-exact match => #{variant_bare}")
                return variant_bare
              end
            end
            prefix_len = 40
            if norm_nameless_displayed.length >= prefix_len
              norm_prefix = norm_nameless_displayed[0, prefix_len]
              variants.each do |variant_bare|
                stem = variant_bare.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
                map_text = resolver2.call(entry_map[stem])
                next unless map_text
                if _normalize_text(map_text).start_with?(norm_prefix)
                  log("  _match_variant: #{tag2}nameless-prefix match => #{variant_bare}")
                  return variant_bare
                end
              end
            end
            displayed_words2 = norm_nameless_displayed.split(" ").select { |w| w.length >= 4 }.uniq
            if displayed_words2.length >= 2
              best_v2 = nil
              best_r2 = 0.0
              variants.each do |variant_bare|
                stem = variant_bare.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
                map_text = resolver2.call(entry_map[stem])
                next unless map_text
                norm_map = _normalize_text(map_text)
                map_words2 = norm_map.split(" ").select { |w| w.length >= 4 }.uniq
                next if map_words2.empty?
                common2 = (displayed_words2 & map_words2).length
                ratio2 = common2.to_f / [displayed_words2.length, map_words2.length].max
                if ratio2 > best_r2
                  best_r2 = ratio2
                  best_v2 = variant_bare
                end
              end
              if best_v2 && best_r2 >= 0.35
                log("  _match_variant: #{tag2}nameless-word-overlap (ratio=#{best_r2.round(2)}) => #{best_v2}")
                return best_v2
              end
            end
            nil
          end
          result = _try_match_nameless.call(nameless_resolver, "POKENAME-stripped ")
          return result if result
        end
      end
    end

    log("  _match_variant: no match found")
    nil
  end

  # Longest-common-subsequence ratio between two strings.
  # Returns a Float in 0.0..1.0 indicating similarity.
  # Uses an optimised two-row DP to keep memory usage low.
  def self._lcs_ratio(a, b)
    return 1.0 if a == b
    return 0.0 if a.empty? || b.empty?
    # Ensure a is the shorter string for the two-row optimisation
    a, b = b, a if a.length > b.length
    prev = Array.new(a.length + 1, 0)
    curr = Array.new(a.length + 1, 0)
    b.length.times do |i|
      a.length.times do |j|
        if b[i] == a[j]
          curr[j + 1] = prev[j] + 1
        else
          curr[j + 1] = curr[j] > prev[j + 1] ? curr[j] : prev[j + 1]
        end
      end
      prev, curr = curr, prev
      curr.fill(0)
    end
    lcs_len = prev[a.length]
    lcs_len.to_f / [a.length, b.length].max
  end

  # Play a single audio file (bare_name is relative to Audio/SE/).
  # Stops any currently playing SE first.
  #
  # Tries the bare path first (standard RGSS resolves relative to Audio/SE/),
  # then falls back to the full "Audio/SE/" prefixed path in case the engine
  # needs it (some MKXP builds behave differently).
  def self._play_bare(bare_name)
    # REMOVED: Audio.se_stop — the generation counter in play() already handles
    # cancelling previous playback. Calling se_stop here kills audio from the
    # current generation's own play attempt when multiple threads race.
    log("  _play_bare: Audio.se_play('#{bare_name}', #{volume}, 100)")
    begin
      Audio.se_play(bare_name, volume, 100)
      return
    rescue StandardError => e
      log("  _play_bare: bare path failed (#{e.class}: #{e.message})")
    end
    # Fallback: try with "Audio/SE/" prefix
    full = "Audio/SE/#{bare_name}"
    log("  _play_bare: retrying with Audio.se_play('#{full}', #{volume}, 100)")
    begin
      Audio.se_play(full, volume, 100)
    rescue StandardError => e
      log("  _play_bare: full path also failed (#{e.class}: #{e.message})")
    end
  end

