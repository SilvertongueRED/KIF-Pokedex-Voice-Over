  # Gather all base Pokédex entry texts recorded for a species, trying
  # GameData (live game data) first and then the entry_map (pre-generated
  # audio manifest) as a fallback.  Returns an Array of unique Strings.
  def self._base_dex_texts(species_name, species_id)
    texts = []

    # Strategy 1: GameData description methods (Pokémon Essentials / KIF)
    begin
      if defined?(GameData::Species)
        data = GameData::Species.try_get(species_id)
        if data
          [:description, :pokedex_entry, :dex_entry].each do |meth|
            if data.respond_to?(meth)
              text = data.send(meth)
              texts << text if text.is_a?(String) && !text.strip.empty?
            end
          end
        end
      end
    rescue StandardError => e
      log("  _base_dex_texts: GameData error for #{species_name}: #{e.message}")
    end

    # Strategy 2: entry_map lookup — covers all recorded variants for this
    # species (e.g. dex_RATTATA, dex_RATTATA_v2, etc.).
    unless entry_map.empty?
      entry_map.each do |key, val|
        next unless val.is_a?(String) && !val.strip.empty?
        # Match keys like dex_RATTATA, dex_RATTATA_v2, but NOT dex_RATTATA_PIDGEY
        if key =~ /\Adex_#{Regexp.escape(species_name.upcase)}(_v\d+)?\z/i
          texts << val
        end
      end
    end

    texts.uniq
  end

  # Returns true when *displayed_text* appears to be an auto-generated KIF
  # fusion entry — i.e., it is composed entirely of sentences drawn from the
  # base Pokédex entries of *head_species* and *body_species*, which KIF
  # randomly mashes together every time the Pokédex page is opened.
  #
  # When this returns true the correct audio approach is sequential
  # base-species playback (head then body), because no pre-recorded fusion
  # audio file can reliably match text that changes on every page visit.
  def self._autogenerated_fusion_entry?(displayed_text, head_species, body_species)
    return false unless displayed_text.is_a?(String) && !displayed_text.strip.empty?
    return false unless head_species && body_species

    head_str = species_str(head_species)
    body_str = species_str(body_species)
    return false unless head_str && body_str

    head_texts = _base_dex_texts(head_str, head_species)
    body_texts = _base_dex_texts(body_str, body_species)

    if head_texts.empty? && body_texts.empty?
      log("  _autogenerated_fusion_entry?: no base texts found for #{head_str}/#{body_str} — cannot determine")
      return false
    end

    # Build a pool of normalized sentences from both base entries.
    # Sentences are split on .  !  ? boundaries.
    sentence_pool = []
    (head_texts + body_texts).each do |entry|
      # Capture terminated sentences, plus any trailing text without punctuation
      parts = entry.scan(/[^.!?]+[.!?]+/)
      last_term = entry.rindex(/[.!?]/)
      if last_term
        trailing = entry[(last_term + 1)..].strip
        parts << trailing unless trailing.empty?
      elsif !entry.strip.empty?
        parts << entry.strip
      end
      parts.each do |s|
        norm = _normalize_text(s.strip)
        sentence_pool << norm unless norm.empty?
      end
    end

    if sentence_pool.empty?
      log("  _autogenerated_fusion_entry?: empty sentence pool for #{head_str}/#{body_str}")
      return false
    end

    # Split the displayed text into sentences.
    # Include any trailing fragment without terminal punctuation.
    displayed_parts = displayed_text.scan(/[^.!?]+[.!?]+/)
    last_term = displayed_text.rindex(/[.!?]/)
    if last_term
      trailing = displayed_text[(last_term + 1)..].strip
      displayed_parts << trailing unless trailing.empty?
    elsif !displayed_text.strip.empty?
      displayed_parts << displayed_text.strip
    end
    displayed_sentences = displayed_parts.map { |s| _normalize_text(s.strip) }.reject(&:empty?)

    if displayed_sentences.empty?
      log("  _autogenerated_fusion_entry?: could not split displayed text into sentences")
      return false
    end

    log("  _autogenerated_fusion_entry?: checking #{displayed_sentences.length} sentences against pool of #{sentence_pool.length} base sentences (#{head_str}/#{body_str})")

    # Auto-generated if every displayed sentence is present in the base pool.
    # Use exact normalized equality only — substring matching risks false
    # positives when custom entries share phrases with base species' entries.
    result = displayed_sentences.all? do |s|
      sentence_pool.any? { |p| p == s }
    end
    log("  _autogenerated_fusion_entry? => #{result}")
    result
  end

  # -------------------------------------------------------------------------
  # Playback
  # -------------------------------------------------------------------------

  # Play the voice-over for *species* (and optionally its fusion partner).
  #
  # For fused Pokémon:
  #   1. If the displayed text is detected as auto-generated (randomly mashed
  #      from both base species' entries by KIF), skip fusion audio entirely
  #      and use sequential base-species playback (head then body).
  #   2. If a dedicated fusion audio file exists AND its text matches
  #      *displayed_text*, play that file (chosen via _match_variant).
  #      This check always runs even when only 1 variant exists, because
  #      the single file may have been generated for a different text.
  #   3. If fusion files exist but none match, fall back to sequential
  #      base-species playback (head then body) when at least one base file
  #      exists.
  #   4. If no fusion files exist at all, try sequential base-species
  #      playback directly.
  #   5. Last resort (no base audio available): play a random fusion variant.
  #
  # NOTE: The reverse-ordering lookup (dex_BODY_HEAD) has been intentionally
  # removed.  dex_HOOTHOOT_RATTATA is audio for Hootat (Hoothoot head +
  # Rattata body), NOT for Rathoot (Rattata head + Hoothoot body).  Using
  # the reversed file would narrate the wrong Pokémon's entry entirely.
  #
  # Returns silently if no matching audio can be found.
  def self.play(species, fused_species = nil, displayed_text = nil)
    log("play() called — enabled=#{enabled?}, species=#{species.inspect}, fused=#{fused_species.inspect}, text=#{displayed_text ? displayed_text[0,40].inspect : 'nil'}")
    return unless enabled?

    base = species_str(species)
    log("  base species_str => #{base.inspect}")
    return unless base

    fused = species_str(fused_species)
    log("  fused species_str => #{fused.inspect}")

    # Advance the generation counter to invalidate any in-progress sequential
    # playback thread without forcibly killing it.
    @playback_gen = (@playback_gen || 0) + 1
    my_gen = @playback_gen  # snapshot so the thread can self-cancel

    # ------------------------------------------------------------------
    # Resolve which file(s) to play on the MAIN THREAD.
    # All file-existence checks, variant matching, entry_map / durations
    # loading, and JSON parsing happen here — these operations are not
    # thread-safe in MKXP-EX (the Ruby runtime used by KIF) because
    # `require` can deadlock when called from a background thread.
    # ------------------------------------------------------------------
    # playback_mode: :single, :sequential, or :none
    playback_mode = :none
    chosen_file   = nil   # for :single mode
    seq_head      = nil   # for :sequential mode
    seq_body      = nil
    seq_head_dur  = nil
    play_vol      = volume

    begin
      if fused
        fusion_bare = "#{AUDIO_SUBDIR}/dex_#{base}_#{fused}"

        # Check for the primary fusion file and any variants.
        # NOTE: The reversed lookup (dex_BODY_HEAD) has been intentionally
        # removed — see the method comment above for why.
        fusion_variants = _find_variants(fusion_bare)

        # Base-species paths used by sequential fallback (shared across branches).
        bare_head = "#{AUDIO_SUBDIR}/dex_#{base}"
        bare_body = "#{AUDIO_SUBDIR}/dex_#{fused}"

        # Step 1: detect auto-generated entries.  KIF randomly mashes sentences
        # from both base species each time the dex page opens, so no
        # pre-recorded fusion file can reliably match what is on screen.
        # Skip fusion audio entirely and go straight to sequential playback.
        autogen = _autogenerated_fusion_entry?(displayed_text, species, fused_species)

        # Step 2: try text-matched fusion audio (only for non-auto-generated entries).
        if !autogen && fusion_variants.any?
          # Always attempt text matching, even with exactly 1 variant.
          # A single file may have been generated for a different text than
          # what is currently displayed (auto-generated entries differ every visit).
          matched = _match_variant(fusion_variants, displayed_text, species, fused_species)
          if matched.nil? && entry_map.empty?
            log("  WARNING: Fusion audio variant(s) found but dex_entry_map.json is missing — cannot match displayed text to correct variant. Run tools/generate_voices.py to generate the entry map.")
          end
          if matched
            chosen_file = matched
            playback_mode = :single
            log("  Playing fusion audio: #{chosen_file} (from #{fusion_variants.length} variant(s), matched=true)")
          else
            log("  No fusion variant matched displayed text — falling back to sequential base-species playback")
          end
        end

        # Step 2.4: Fish-Speech sidecar - primary on-the-fly TTS backend.
        # Voice-clones the displayed text via the open-source fish-speech
        # engine running as a local HTTP server.  Tried BEFORE Piper because
        # the cloned voice is much closer to the Pokedex narrator.  Falls
        # through silently when the sidecar is not running or disabled.
        if playback_mode == :none && fish_speech_enabled?
          if displayed_text && !displayed_text.strip.empty?
            unless fish_speech_available?
              # Server not up - try to auto-launch in the background.  This
              # request will fall through to Piper; subsequent requests will
              # find the server alive once it finishes loading (~10-30s).
              _fish_speech_try_autostart
            end
            if fish_speech_available?
              # Queue TTS asynchronously — generation runs in the background
              # thread so the entry screen isn't frozen during synthesis.
              log("  Step 2.4: Fish-Speech available — queuing async TTS (#{displayed_text.length} chars)")
              playback_mode = :fish_speech_async
            else
              log("  Step 2.4: Fish-Speech sidecar unavailable - falling through to Piper")
            end
          else
            log("  Step 2.4: skipping Fish-Speech - no displayed text available")
          end
        end

        # Step 2.5: Piper TTS fallback — generate audio on-the-fly from the
        # displayed text.  Sits between the pre-recorded fusion audio check
        # (step 2) and the sequential base-species fallback (step 3) so the
        # player hears something that actually matches what is on screen.
        # Only runs when piper is installed and the TTS setting is enabled.
        if playback_mode == :none && piper_available? && tts_enabled?
          if displayed_text && !displayed_text.strip.empty?
            log("  Step 2.5: attempting Piper TTS for displayed text (#{displayed_text.length} chars)")
            tts_bare = _generate_tts(displayed_text)
            if tts_bare
              chosen_file   = tts_bare
              playback_mode = :single
              log("  Playing Piper TTS audio: #{chosen_file}")
            else
              log("  Piper TTS generation failed — falling through to sequential base-species playback")
            end
          else
            log("  Step 2.5: skipping Piper TTS — no displayed text available")
          end
        end

        # Step 3: sequential base-species fallback — reached when:
        #   a) auto-generated entry detected, OR
        #   b) no fusion audio files exist, OR
        #   c) fusion audio files exist but none matched the displayed text.
        if playback_mode == :none
          head_exists = _audio_exists?(bare_head)
          body_exists = _audio_exists?(bare_body)
          if head_exists && body_exists
            seq_head = bare_head
            seq_body = bare_body
            seq_head_dur = (durations["dex_#{base}.ogg"] || SEQUENTIAL_DURATION_FALLBACK).to_f
            playback_mode = :sequential
            log("  Playing sequential: #{seq_head} then #{seq_body}")
          elsif head_exists
            chosen_file = bare_head
            playback_mode = :single
            log("  Only head base audio available — playing: #{chosen_file}")
          elsif body_exists
            chosen_file = bare_body
            playback_mode = :single
            log("  Only body base audio available — playing: #{chosen_file}")
          elsif fusion_variants.any?
            # Last resort: no base audio at all — play a random fusion variant
            # rather than staying silent.
            chosen_file = fusion_variants.sample
            playback_mode = :single
            log("  No base species audio available — playing random fusion variant: #{chosen_file} (from #{fusion_variants.length} variant(s))")
          else
            log("  No fusion or base-species audio available — skipping")
          end
        end
      else
        bare = "#{AUDIO_SUBDIR}/dex_#{base}"

        # Step A: Fish-Speech for non-fusion entries.  When the user has the
        # sidecar running we prefer cloned-voice TTS over pre-recorded files
        # too - this is what makes the mod work without ANY pre-generated
        # audio files for regular Pokemon (custom or auto-generated entries
        # included).  Pre-recorded audio still wins when Fish-Speech is off
        # or unavailable.
        if fish_speech_enabled? && displayed_text && !displayed_text.strip.empty?
          unless fish_speech_available?
            _fish_speech_try_autostart
          end
          if fish_speech_available?
            # Queue TTS asynchronously — generation happens in the background
            # thread in parallel with the Pokémon cry, so the entry screen
            # is never blocked waiting for audio to generate.
            log("  [regular] Fish-Speech available — queuing async TTS (#{displayed_text.length} chars)")
            playback_mode = :fish_speech_async
          end
        end

        # Step B: pre-recorded variants (legacy path - still used when
        # Fish-Speech is disabled, unavailable, or returned an error).
        if playback_mode == :none
          variants = _find_variants(bare)

          if variants.empty?
            # Step C: Piper TTS last-resort for regular Pokemon when no
            # pre-recorded audio exists either.
            if piper_available? && tts_enabled? && displayed_text && !displayed_text.strip.empty?
              log("  [regular] No pre-recorded audio; attempting Piper TTS (#{displayed_text.length} chars)")
              tts_bare = _generate_tts(displayed_text)
              if tts_bare
                chosen_file = tts_bare
                playback_mode = :single
                log("  Playing Piper TTS audio: #{chosen_file}")
              end
            end

            if playback_mode == :none
              log("  No audio file found - skipping")
              return
            end
          else
            matched = _match_variant(variants, displayed_text)
            if matched.nil? && variants.length > 1 && entry_map.empty?
              log("  WARNING: Multiple audio variants found but dex_entry_map.json is missing - cannot match displayed text to correct variant. Run tools/generate_voices.py to generate the entry map. Falling back to random selection.")
            end
            chosen_file = matched || variants.sample
            playback_mode = :single
            log("  Playing species audio: #{chosen_file} (from #{variants.length} variant(s), matched=#{!matched.nil?})")
          end
        end
      end
    rescue StandardError => e
      log("  File resolution error: #{e.class}: #{e.message}")
      return
    end

    return if playback_mode == :none
    # :fish_speech_async — we don't have a file yet; duration is computed
    # inside the background thread after TTS completes.

    # ------------------------------------------------------------------
    # Compute voiceover duration on the MAIN THREAD (needs file I/O for
    # durations manifest) so the background thread can sleep for the right
    # amount of time before restoring BGM.
    # ------------------------------------------------------------------
    vo_duration = nil
    if mute_during_voiceover?
      begin
        case playback_mode
        when :single
          # Strip subdir prefix to get the bare filename used in durations manifest
          stem = chosen_file.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
          manifest_dur = durations["#{stem}.ogg"] || durations["#{stem}.wav"]
          if manifest_dur
            vo_duration = manifest_dur.to_f
          elsif chosen_file.include?("tts_cache")
            # Piper-generated WAV — read actual duration from the WAV header
            wav_abs = "Audio/SE/#{chosen_file}.wav"
            vo_duration = FileTest.exist?(wav_abs) ? _tts_audio_duration(wav_abs) : SEQUENTIAL_DURATION_FALLBACK
          else
            vo_duration = SEQUENTIAL_DURATION_FALLBACK.to_f
          end
          log("  Voiceover duration for #{stem}: #{vo_duration}s")
        when :sequential
          head_dur = seq_head_dur || SEQUENTIAL_DURATION_FALLBACK
          body_stem = seq_body.sub(/\A#{Regexp.escape(AUDIO_SUBDIR)}\//, "")
          body_dur = (durations["#{body_stem}.ogg"] || SEQUENTIAL_DURATION_FALLBACK).to_f
          vo_duration = head_dur + body_dur
          log("  Voiceover total sequential duration: #{vo_duration}s (head=#{head_dur}s, body=#{body_dur}s)")
        end
      rescue StandardError => e
        log("  Voiceover duration lookup error: #{e.message}")
        vo_duration = SEQUENTIAL_DURATION_FALLBACK
      end
    end

    # ------------------------------------------------------------------
    # Background thread — handles TTS generation (async), cry delay, and
    # Audio.se_play.  When playback_mode is :fish_speech_async the HTTP
    # call to the TTS server runs in a nested thread so the cry delay and
    # TTS synthesis happen in parallel rather than sequentially.
    # ------------------------------------------------------------------
    tts_text_snap = displayed_text  # freeze for the closure
    # Pre-split sentences so the streaming path can generate them one at a time.
    # A single-sentence entry or non-async mode skips straight to the simple path.
    tts_sentences_snap = (playback_mode == :fish_speech_async) ?
                          PokedexVoiceOver._split_sentences(tts_text_snap) : nil
    begin
      Thread.new do
        begin
          if playback_mode == :fish_speech_async && tts_sentences_snap && tts_sentences_snap.length > 1
            # ---------------------------------------------------------------
            # SENTENCE STREAMING MODE
            # Generate sentence[0] in parallel with the cry delay, play it as
            # soon as it is ready, then generate sentence[1] while sentence[0]
            # is playing, and so on.  The perceived wait drops from "full entry
            # generation time" to "first sentence generation time" (~40-60% of
            # the original wait for a typical 2-3 sentence entry).
            # ---------------------------------------------------------------
            sentences  = tts_sentences_snap
            s_results  = Array.new(sentences.length) { nil }

            PokedexVoiceOver.log("  Streaming TTS: #{sentences.length} sentences (#{tts_text_snap.length} chars total)")

            # Kick off sentence[0] synthesis immediately — runs in parallel
            # with the cry delay so the player experiences zero additional wait.
            s0_thread = Thread.new do
              s_results[0] = PokedexVoiceOver._generate_tts_fish_speech(sentences[0])
            end

            _interruptible_sleep(CRY_DELAY, my_gen)
            if @playback_gen != my_gen
              # CRASH FIX: never call Thread#kill on a thread that may be
              # mid-HTTP-read.  MRI's docs flag kill as unsafe, and inside
              # MKXP-Z's embedded interpreter killing a TCPSocket-reading
              # thread is a known segfault trigger (= "hard crash to desktop"
              # when the user opens the Pokedex while a request is in flight).
              # The orphaned thread will finish its synthesis on its own and
              # the resulting WAV will be ready in the cache next visit.
              PokedexVoiceOver.log("  Streaming TTS: playback gen changed — orphaning sentence 0 thread")
              next
            end

            s0_thread.join
            if s_results[0].nil?
              PokedexVoiceOver.log("  Streaming TTS: sentence 0 failed — aborting")
              next
            end

            save_and_mute_bgm_for_voiceover

            sentences.each_with_index do |_sentence, i|
              break if @playback_gen != my_gen

              bare = s_results[i]
              break unless bare  # this sentence failed to generate

              # Launch synthesis of the NEXT sentence in parallel with playing
              # the CURRENT one — by the time the current sentence finishes
              # playing, the next one is already generated (or nearly so).
              next_thread = nil
              if i + 1 < sentences.length
                next_i   = i + 1
                next_txt = sentences[next_i]
                next_thread = Thread.new do
                  s_results[next_i] = PokedexVoiceOver._generate_tts_fish_speech(next_txt)
                end
              end

              # Play this sentence and wait for it to finish.
              _play_bare(bare)
              wav_abs = "Audio/SE/#{bare}.wav"
              dur = FileTest.exist?(wav_abs) ?
                      PokedexVoiceOver._tts_audio_duration(wav_abs) :
                      PokedexVoiceOver::SEQUENTIAL_DURATION_FALLBACK
              PokedexVoiceOver.log("  Streaming TTS: sentence #{i + 1}/#{sentences.length} playing (#{dur.round(2)}s)")

              _interruptible_sleep(dur, my_gen)

              if @playback_gen != my_gen
                # See the comment on the s0_thread orphan path above —
                # Thread#kill on a TCPSocket-reading thread crashes MKXP-Z.
                PokedexVoiceOver.log("  Streaming TTS: playback gen changed mid-stream — orphaning prefetch thread")
                break
              end

              next_thread&.join
            end

            restore_bgm_after_voiceover if @vo_bgm_muted

          else
            # ---------------------------------------------------------------
            # SINGLE-TEXT / SEQUENTIAL MODE (original logic)
            # Used for: single-sentence entries, non-fish-speech modes,
            # sequential base-species fallback.
            # ---------------------------------------------------------------
            tts_inner   = nil
            tts_result  = [nil]   # [file_path]
            if playback_mode == :fish_speech_async
              tts_result_ref = tts_result
              tts_inner = Thread.new do
                tts_result_ref[0] = PokedexVoiceOver._generate_tts_fish_speech(tts_text_snap)
              end
            end

            # Wait for the Pokémon's cry to finish before reading the entry.
            _interruptible_sleep(CRY_DELAY, my_gen)

            if @playback_gen != my_gen
              # See streaming-mode comment — Thread#kill on a socket-reading
              # thread is unsafe in MKXP-Z (segfault).  Let it finish in the
              # background; its cache write is harmless.
              PokedexVoiceOver.log("  Single-mode Fish-Speech: playback gen changed — orphaning TTS thread")
              next
            end

            # Resolve async TTS result (join nested thread, update local vars).
            if playback_mode == :fish_speech_async
              tts_inner.join
              fs_bare = tts_result[0]
              if fs_bare
                chosen_file   = fs_bare
                playback_mode = :single
                log("  Async Fish-Speech TTS ready: #{chosen_file}")
                # Calculate voice-over duration now that the WAV exists.
                if mute_during_voiceover? && vo_duration.nil?
                  wav_abs = "Audio/SE/#{chosen_file}.wav"
                  vo_duration = FileTest.exist?(wav_abs) ? _tts_audio_duration(wav_abs) : SEQUENTIAL_DURATION_FALLBACK
                end
              else
                log("  Async Fish-Speech TTS failed — no audio for this entry")
                next
              end
            end

            next if @playback_gen != my_gen

            # Mute BGM for the duration of the voice-over playback
            save_and_mute_bgm_for_voiceover

            case playback_mode
            when :single
              _play_bare(chosen_file)
            when :sequential
              _play_bare(seq_head)
              _interruptible_sleep(seq_head_dur, my_gen)
              if @playback_gen == my_gen
                _play_bare(seq_body)
              end
            end

            # Wait for the voice-over to finish, then restore BGM
            if @vo_bgm_muted && vo_duration
              _interruptible_sleep(vo_duration, my_gen)
              restore_bgm_after_voiceover
            end
          end
        rescue StandardError => e
          PokedexVoiceOver.log("  Playback thread error: #{e.class}: #{e.message}")
        ensure
          # Safety net: always restore BGM if it was muted for voiceover
          # and the thread is exiting (e.g. due to generation change or error)
          restore_bgm_after_voiceover if @vo_bgm_muted
        end
      end
    rescue StandardError => e
      log("  Thread creation error: #{e.class}: #{e.message}")
    end
  end

  # Sleep in small increments so we respond promptly to new play/stop calls
  # (which bump @playback_gen).  Returns early if the generation changes.
  def self._interruptible_sleep(duration, gen)
    elapsed = 0.0
    step = 0.1
    while elapsed < duration
      break if @playback_gen != gen
      sleep(step)
      elapsed += step
    end
  end

  # Stop any playing voice-over, including any pending sequential playback.
  # Bumping @playback_gen causes any running playback thread to exit cleanly.
  # NOTE: Scene-level BGM is managed by the scene hooks (muted on open,
  # restored on close).  Voiceover-level BGM is restored here in case stop()
  # is called before the playback thread finishes its restore.
  def self.stop
    @playback_gen = (@playback_gen || 0) + 1
    begin
      Audio.se_stop
    rescue StandardError => e
      log("stop error: #{e.class}: #{e.message}")
    end
    restore_bgm_after_voiceover
  end
end

